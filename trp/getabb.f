      SUBROUTINE GETABB(T2ABB,IRPJ,J,LAMBDA)
      IMPLICIT INTEGER (A-Z)
      DOUBLE PRECISION T2ABB(1)
      DIMENSION IADT2(8),LENT2(8)
      LOGICAL LAMBDA
C
      COMMON /MACHSP/ IINTLN,IFLTLN,IINTFP,IALONE,IBITWD
      COMMON /SYMINF/ NSTART,NIRREP,IRREPA(255),IRREPB(255),
     1                DIRPRD(8,8)
      COMMON /SYMPOP/ IRPDPD(8,22),ISYTYP(2,500),ID(18)
      COMMON /SYM/    POP(8,2),VRT(8,2),NTAA,NTBB,NF1AA,NF2AA,
     1                NF1BB,NF2BB
      COMMON /INFO/   NOCCO(2),NVRTO(2)
      COMMON /FLAGS/  IFLAGS(100)
      EQUIVALENCE(ICLLVL,IFLAGS( 2))
      EQUIVALENCE(IDRLVL,IFLAGS( 3))
      EQUIVALENCE(IREFNC,IFLAGS(11))
C
      COMMON /T3OFF/  IOFFVV(8,8,10),IOFFOO(8,8,10),IOFFVO(8,8,4)
C
      INDEX(I) = I*(I-1)/2
C     List offset
      IF (LAMBDA) THEN
         LISTOFF=100
      ELSE
         LISTOFF=0
      END IF
C
C     SET ADDRESSES FOR T2.
C
      DO  10 IRPL=1,NIRREP
      IRPJL = DIRPRD(IRPL,IRPJ)
      IRPBC = IRPJL
      LENT2(IRPL) = IRPDPD(IRPBC,13) * POP(IRPL,2)
      IF(IRPL.EQ.1)THEN
      IADT2(IRPL) = 1
      ELSE
      IADT2(IRPL) = IADT2(IRPL-1) + LENT2(IRPL-1)
      ENDIF
   10 CONTINUE
C
C     READ T2(BC,L).
C
      DO   30 IRPL=1,NIRREP
      IF(POP(IRPL,2).EQ.0) GOTO 30
      IRPJL = DIRPRD(IRPJ,IRPL)
C
      DO   20    L=1,POP(IRPL,2)
      JL = IOFFOO(IRPL,IRPJL,5) + (L-1)*POP(IRPJ,1) + J
      CALL GETLST(T2ABB(IADT2(IRPL) + (L-1)*IRPDPD(IRPJL,13)),
     1            JL,1,1,IRPJL,46+LISTOFF)
   20 CONTINUE
   30 CONTINUE
C
      RETURN
      END
