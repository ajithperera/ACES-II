      SUBROUTINE FORM_HT3C3(T3,W,SCR1,SCR2,SCR3,ICORE,OOOV,OOOV2,
     &                      OOOVAD,LNOOOV,IADBLK,IADW,IADW2,IRPI,IRPJ,
     &                      IRPK,IRPIJ,IRPIK,IRPJK,IRPIJK,I,J,K,
     &                      LNVOIJ,LNVOIK,LNVOJK,LNVOIK2,LNVOJK2,
     &                      ICLLVL,LAMBDA,NONHF)
      IMPLICIT INTEGER (A-Z)
      LOGICAL LAMBDA
      LOGICAL NONHF 
      DOUBLE PRECISION T3(1),W(1),ICORE(1),SCR1(1),SCR2(1),SCR3(1)
      DOUBLE PRECISION OOOV(LNOOOV,4),OOOV2(LNOOOV,4)
      DIMENSION IADT3(8)
      DIMENSION OOOVAD(8,4)
      DIMENSION IADFVO(8),LENFVO(8)
      DIMENSION LEN(8,8),IADBLK(8),LENBLK(8)
      DIMENSION IADT2(8),LENT2(8)
      DIMENSION IADV(8),LENV(8)
      DIMENSION IADW(8),LENW(8)
      DIMENSION IADW2(8)

      COMMON /MACHSP/ IINTLN,IFLTLN,IINTFP,IALONE,IBITWD
      COMMON /SYMINF/ NSTART,NIRREP,IRREPA(255),IRREPB(255),
     1                DIRPRD(8,8)
      COMMON /SYMPOP/ IRPDPD(8,22),ISYTYP(2,500),ID(18)
      COMMON /SYM/    POP(8,2),VRT(8,2),NTAA,NTBB,NF1AA,NF2AA,
     1                NF1BB,NF2BB
      COMMON /INFO/   NOCCO(2),NVRTO(2)
C
      COMMON /T3OFF/  IOFFVV(8,8,10),IOFFOO(8,8,10),IOFFVO(8,8,4)
      COMMON /T2ILIS/ LIST2I1,LIST2I2,LIST2I3
C
      INDEX(I) = I*(I-1)/2

      ISTART = 1

      IF (NONHF) THEN
          CALL T2FT3_HT3C3(T3,W,ICORE(ISTART),IADBLK,IADW2,IRPI,
     &                     IRPJ,IRPK,IRPIJ,IRPIK,IRPJK,IRPIJK,I,
     &                     J,K,SCR1,SCR2,SCR3,ICLLVL,0,LAMBDA)
      ENDIF 
C
C     --- D2T2 = W T3 ---
C
      CALL T2T3_HT3C3_1(T3,W,ICORE(ISTART),
     &                  IADBLK,IADW2,IRPI,IRPJ,IRPK,IRPIJ,IRPIK,
     &                  IRPJK,IRPIJK,I,J,K,LAMBDA)
C
      CALL T2T3_HT3C3_2(T3,W,ICORE(ISTART),
     & OOOV2(OOOVAD(IRPIJ,2)+IOFFOO(IRPJ,IRPIJ,2)*IRPDPD(IRPIJ,17),2),
     & OOOV2(OOOVAD(IRPIK,3) + IOFFOO(IRPI,IRPIK,5)*LNVOIK,3),
     & OOOV2(OOOVAD(IRPJK,3) + IOFFOO(IRPJ,IRPJK,5)*LNVOJK,3),
     & OOOV2(OOOVAD(IRPIK,4) + IOFFOO(IRPI,IRPIK,5)*LNVOIK2,4),
     & OOOV2(OOOVAD(IRPJK,4) + IOFFOO(IRPJ,IRPJK,5)*LNVOJK2,4),
     & IADBLK,IADW2,IRPI,IRPJ,IRPK,IRPIJ,IRPIK,IRPJK,IRPIJK,I,J,K,
     & LNVOIJ,LNVOIK,LNVOJK,LNVOIK2,LNVOJK2,LAMBDA)

      RETURN
      END 
