      SUBROUTINE DEWT1(ICORE,MAXCOR,IUHF,DVORML)
      IMPLICIT INTEGER (A-Z)
      DIMENSION ICORE(MAXCOR)
      COMMON /INFO/ NOCCO(2),NVRTO(2)
      COMMON /MACHSP/ IINTLN,IFLTLN,IINTFP,IALONE,IBITWD
      COMMON /SYM/    POP(8,2),VRT(8,2),NTAA,NTBB,NF1AA,NF2AA,
     1                NF1BB,NF2BB
      COMMON /SYMINF/ NSTART,NIRREP,IRREPA(255),IRREPB(255),
     &                DIRPRD(8,8)
      COMMON /SYMPOP/ IRPDPD(8,22),ISYTYP(2,500),ID(18)
C
      IF(DVORML.EQ.0)THEN
      WRITE(6,1000)
 1000 FORMAT(' @DEWT1-I, T ---> DT ')
      ELSE
      WRITE(6,1010)
 1010 FORMAT(' @DEWT1-I, DT ---> T ')
      ENDIF
C
      DO 10 ISPIN=1,IUHF+1
C
      IF(ISPIN.EQ.1)THEN
      NT1 = NTAA
      ELSE
      NT1 = NTBB
      ENDIF
C
      NOCC = NOCCO(ISPIN)
      NVRT = NVRTO(ISPIN)
C
      I000 = 1
      I010 = I000 + IINTFP * NT1
      I020 = I010 + IINTFP * NT1
      I030 = I020 + IINTFP * NT1
      I040 = I030 + IINTFP * (NOCC + NVRT)
C
      IF(I040.GT.MAXCOR)THEN
       CALL INSMEM('DEWT1',I040,MAXCOR)
      ELSE
      CALL GETLST(ICORE(I010),1,1,1,2+ISPIN,90)
      CALL DEWT1A(ICORE(I000),ICORE(I010),ICORE(I020),ICORE(I030),
     1            NT1,POP(1,ISPIN),VRT(1,ISPIN),NOCC,NVRT,ISPIN,
     1            DVORML)
      CALL PUTLST(ICORE(I000),1,1,1,2+ISPIN,90)
      ENDIF
   10 CONTINUE
C
      RETURN
      END
