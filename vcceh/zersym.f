C
      SUBROUTINE ZERSYM(Z, NLIST)
      IMPLICIT INTEGER (A-Z)
      DOUBLE PRECISION Z(1)
C
      COMMON /SYMINF/ NSTART,NIRREP,IRREPA(255),IRREB(255),
     &                DIRPRD(8,8)
      COMMON /SYMPOP/ IRPDPD(8,22),ISYTYP(2,500),ID(18)
C
      DO 10 IRREP = 1, NIRREP
         NSYDIS = IRPDPD(IRREP, ISYTYP(2, NLIST))
         NSYDSZ = IRPDPD(IRREP, ISYTYP(1, NLIST))
         CALL ZERO(Z, NSYDIS*NSYDSZ)
         CALL PUTLST(Z, 1, NSYDIS, 1, IRREP, NLIST)
 10   CONTINUE
C
      RETURN
      END
