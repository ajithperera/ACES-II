      
      SUBROUTINE HDIVIDEDD(SCR, MAXCOR, IUHF, IRREPX, NSIZEC)
C
C  THE DIAGONAL OF HBAR^-1 * - DOUBLE COMPONENTS  OF INCREMENT VECTOR
c IS CALCULATED, AND PUT IN PLACE OF THE INPUT VECTOR
C
      IMPLICIT DOUBLE PRECISION (A-H,O-Z)
      DIMENSION SCR(MAXCOR)
C
      COMMON /SYMPOP/ IRPDPD(8,22),ISYTYP(2,500),ID(18)
C
      ONE = 1.0D0
C
      I000 = 1
      I010 = I000 + NSIZEC
      I020 = I010 + NSIZEC
C
      CALL LOADVEC2(IRREPX,SCR,MAXCOR,IUHF,490,2,460,NSIZEC,
     &              .FALSE.)
      CALL GETLST(SCR(I010),1,1,1,1,472)
C
      N1AA = IRPDPD(IRREPX,16)
      IF (IUHF .EQ. 0) THEN
        N1BB = 0
      ELSE
        N1BB = IRPDPD(IRREPX,17)
      ENDIF
      N1 = N1AA + N1BB
C
      DO I = N1, NSIZEC - 1
        SCR(I000 + I) = - SCR(I000+I) / SCR(I010+I)
      ENDDO
C
C  DO NOT CHANGE THE C1 COEFFICENTS
C
      CALL GETLST(SCR(I000), 1,1,1,1,490)
      IF (IUHF .NE. 0) CALL GETLST(SCR(I000+N1AA),1,1,1,2,490)
C
      CALL UPDATES2(IRREPX,SCR(I000),444,0,490,IUHF)
C
      RETURN
      END
