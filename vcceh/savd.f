      SUBROUTINE SAVD(DOOA,DOOB,DVVA,DVVB,DVOA,DVOB,IUHF)
C
C THIS SUBROUTINE SAVES THE RELAXED DENSITY MATRIX ON THE
C GAMLAM FILE. THIS IS ONLY REQUIRED FOR SECOND DERIVATIVE
C CALCULATIONS.
C
C THE LISTS WRITTEN ARE:
C
C  DOOA    1,160
C  DOOB    2,160    UHF AND ROHF ONLY
C  DVVA    3,160
C  DVVB    4,160    UHF AND ROHF ONLY
C  DVOA    5,160  
C  DVOB    6,160    UHF AND ROHF ONLY
C
CEND
C
C CODED APRIL/91 JG
C
      IMPLICIT DOUBLE PRECISION(A-H,O-Z)
      LOGICAL YESNO
      LOGICAL MBPT2,MBPT3,M4DQ,M4SDQ,M4SDTQ,CCD,QCISD,CCSD,UCC
      INTEGER POP,VRT
      CHARACTER*80 FNAME
      DIMENSION DOOA(1),DOOB(1),DVVA(1),DVVB(1),DVOA(1),DVOB(1)
      COMMON/SYM/POP(8,2),VRT(8,2),NT(2),NF1(2),NF2(2)
      COMMON/METH/MBPT2,MBPT3,M4DQ,M4SDQ,M4SDTQ,CCD,QCISD,CCSD,UCC
      COMMON/LISTDENS/LDENS
C
c      CALL GFNAME('GAMLAM  ',FNAME,ILENGTH)
c      INQUIRE(FILE=FNAME(1:ILENGTH),EXIST=YESNO)
c      IF(YESNO) THEN
       IENTER=0
       IOFF=0
c      ELSE
c       IENTER=1
c       IOFF=-1
c      ENDIF
C
      DO 100 ISPIN=1,IUHF+1
C
       CALL UPDMOI(3,NF1(ISPIN),ISPIN,ldens,IENTER,IOFF)
       IENTER=0
       IOFF=0
       CALL UPDMOI(3,NF2(ISPIN),2+ISPIN,ldens,IENTER,IOFF)
       CALL UPDMOI(3,NT(ISPIN),4+ISPIN,ldens,IENTER,IOFF)
C
100   CONTINUE
C
      CALL PUTLST(DOOA,1,1,1,1,ldens)
      CALL PUTLST(DVVA,1,1,1,3,ldens)
      CALL PUTLST(DVOA,1,1,1,5,ldens)
C
      IF(IUHF.NE.0) THEN
C
       CALL PUTLST(DOOB,1,1,1,2,ldens)
       CALL PUTLST(DVVB,1,1,1,4,ldens)
       CALL PUTLST(DVOB,1,1,1,6,ldens)
C
      ENDIF
C
      RETURN
      END
