C
      SUBROUTINE DUMPSYMTRN(SCR, SYMTRN, MAXCENT)
C
C Write the appropriate portion of the SYMTRN matrix to the 'JOBARC'
C file
C
      IMPLICIT DOUBLE PRECISION (A-H, O-Z)
      DIMENSION SCR(NTPERT*NTPERT), SYMTRN(6*MAXCENT, 6*MAXCENT)
C
      LOGICAL JFC, JPSO, JSD, NUCLEI
C
      COMMON /FLAGS/ IFLAGS(100)
      COMMON /MACHSP/ IINTLN, IFLTLN, IINTFP, IALONE, IBITWD
      COMMON /FILES/ LUOUT, MOINTS
      COMMON /NMR/JFC, JPSO, JSD, NUCLEI
      COMMON /PERT/NTPERT, NPERT(8), IPERT(8)
C      
      ITARGET = 1
C
      DO 10 IOFF = 1, NTPERT
C
         CALL SCOPY(NTPERT, SYMTRN(1, IOFF), 1,  SCR(ITARGET), 1)
         ITARGET = ITARGET + NTPERT
C
 10   CONTINUE
C
C Write the transformation matrix in to the JOBARC file
C
      IF (JFC) THEN
        CALL PUTREC (20, 'JOBARC', 'SMTRNFC ', NTPERT*NTPERT*IINTFP,
     &     SCR)
      ELSE IF (JPSO) THEN
        CALL PUTREC (20, 'JOBARC', 'SMTRNPSO', NTPERT*NTPERT*IINTFP,
     &     SCR)
      ELSE IF (JSD) THEN
        CALL PUTREC (20, 'JOBARC', 'SMTRNSD ', NTPERT*NTPERT*IINTFP,
     &     SCR)
      ELSE IF (NUCLEI) THEN
        CALL PUTREC (20, 'JOBARC', 'SMTRNNUC', NTPERT*NTPERT*IINTFP,
     &     SCR)
      ENDIF
C
      RETURN
      END
