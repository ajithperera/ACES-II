
      SUBROUTINE H0QCONT(SCR,MAXCOR,IUHF,IOFF1,LISTZ1,LISTZ2,C0)   
C
C THIS ROUTINE CALCULATES THE CONTRIBUTION <0|H|Q><Q|Z|0> WHERE
C Z IS AN ARBITRARY OPERATOR STORED ON LISTS SPECIFIED BY THE
C INPUT PARAMETERS.
C
CEND
      IMPLICIT DOUBLE PRECISION (A-H,O-Z)
      DIMENSION SCR(MAXCOR)
      COMMON/MACHSP/IINTLN,IFLTLN,IINTFP,IALONE,IBITWD
      COMMON/SYMPOP/IRPDPD(8,22),ISYTYP(2,500),ID(18)
      COMMON/SYM/POP(8,2),VRT(8,2),NT(2),NFMI(2),NFEA(2)
      COMMON/SYMINF/NSTART,NIRREP,IRREPY(255,2),DIRPRD(8,8)
C
C READ IN <Q|Z|0> VECTOR
C
      IOFFC=1
      DO 10 ISPIN=1,1+IUHF
       LENSIN=IRPDPD(1,8+ISPIN)
       CALL GETLST(SCR(IOFFC),1,1,1,IOFF1+ISPIN,LISTZ1)
       IOFFC=IOFFC+LENSIN*IINTFP
10    CONTINUE
      DO 11 ISPIN=3,3-2*IUHF,-1
       LISTD=LISTZ2+ISPIN
       ISIZE=IDSYMSZ(1,ISYTYP(1,LISTD),ISYTYP(2,LISTD))
       CALL GETALL(SCR(IOFFC),ISIZE,1,LISTD)
       IOFFC=IOFFC+ISIZE*IINTFP
11    CONTINUE
      NSIZE=(IOFFC-1) /IINTFP
C
      CALL LOADVEC1(1,SCR(IOFFC),MAXCOR-IOFFC+1,IUHF,93,13,LENTOT)
C
      C0=SDOT(NSIZE,SCR(IOFFC),1,SCR,1)
C
      RETURN
      END
