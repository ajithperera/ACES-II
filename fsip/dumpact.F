      SUBROUTINE DUMPACT(EVAL,NACTIVE,IACTIVE,NBAS,IUHF)
C
C WRITES OUT THE DIAGONAL FOCK MATRIX ELEMENTS FOR THE ACTIVE
C ORBITALS.
C
      IMPLICIT DOUBLE PRECISION (A-H,O-Z)
#include "maxbasfn.par"
      CHARACTER*5 SPCASE(2)
      INTEGER DIRPRD,POP,VRT
      CHARACTER*8 LABEL(2)
      DIMENSION EVAL(1),NACTIVE(2),IACTIVE(MAXBASFN,2)
      COMMON /INFO/NOCCO(2),NVRTO(2)
      COMMON /MACHSP/ IINTLN,IFLTLN,IINTFP,IALONE,IBITWD
      COMMON /SYM/ POP(8,2),VRT(8,2),NT(2),NFMI(2),NFEA(2)
      COMMON /SYMINF/ NSTART,NIRREP,IRREPS(255,2),DIRPRD(8,8)
      DATA LABEL /'SCFEVALA','SCFEVALB'/
      DATA SPCASE/'alpha','beta '/
C
      DO 100 ISPIN=1,1+IUHF
       CALL GETREC(20,'JOBARC',LABEL(ISPIN),NBAS*IINTFP,EVAL)
       WRITE(6,1000)SPCASE(ISPIN)
1000   FORMAT(T3,'Active orbitals for spin case ',A,':')
       WRITE(6,1001)
1001   FORMAT(T8,'Orbital',T25,'Diagonal Fock',/,T10,'No.',
     &        T25,'Matrix Element')
       DO 101 I=1,NACTIVE(ISPIN)
        WRITE(6,1002)IACTIVE(I,ISPIN),EVAL(IACTIVE(I,ISPIN))
1002    FORMAT(T10,I3,T25,F14.8)
101    CONTINUE
100   CONTINUE
C
      RETURN
      END
