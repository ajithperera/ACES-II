      SUBROUTINE TDEE_DRAOLAD(ICORE,MAXCOR,IUHF,TAU,TAU2,IRREPX,
     &                        LISTFLAG,LSTMO,LSTMOINC,LSTAO,
     &                        LSTAOINC,ISIDE)
C
C DRIVER FOR AO-BASED PARTICLE-PARTICLE LADDER CONTRACTION
C
CEND
C LSTMO=313(ISIDE=1),323(ISIDE=2)
C LISMOINC=333(ISIDE=1),343(ISIDE=2)
C LSTAO=374,both sides 
C LISTAOINC=384,both sides
C
      IMPLICIT INTEGER (A-Z)
      DIMENSION ICORE(MAXCOR)
      LOGICAL TAU,TAU2
      COMMON /FLAGS/ IFLAGS(100)
C
#ifdef _DEBUG_LVLM
      Write(6,"(5i5)") LSTMO,LSTMOINC,LSTAOINC,LSTAO,Iside
#endif 
      CALL TDEE_AOLADLST(IUHF,LISTFLAG,IRREPX)
      CALL TDEE_T2TOAO(ICORE,MAXCOR,IUHF,TAU,TAU2,LSTMO,LSTAO,IRREPX)

      IF(IFLAGS(95).EQ.1)THEN
       CALL AOLAD2(ICORE,MAXCOR,IUHF,TAU,IRREPX,LSTAO,LSTAOINC)
      ELSE
       CALL AOLAD3(ICORE,MAXCOR,IUHF,TAU,IRREPX,LSTAO,LSTAOINC)
      ENDIF

      CALL TDEE_Z2TOMO(ICORE,MAXCOR,IUHF,TAU,IRREPX,LSTMO,LSTMOINC,
     &                 LSTAOINC,.FALSE.,ISIDE)
      RETURN
      END
