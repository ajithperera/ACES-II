
      SUBROUTINE PDCC_QUIKAA2(ICORE,MAXCOR,IUHF)
C
C THIS SUBROUTINE FORMS THE ALL ALPHA H(MEJB) INTERMEDIATE
C  FROM THE ABAB AND ABBA PIECES FOR RHF REFERENCE FUNCTIONS.
C
CEND
C
      IMPLICIT DOUBLE PRECISION(A-H,O-Z)
      INTEGER TOTSIZ
      DIMENSION ICORE(MAXCOR)
      COMMON /MACHSP/ IINTLN,IFLTLN,IINTFP,IALONE,IBITWD
      COMMON /SYMPOP/ IRPDPD(8,22),ISYTYP(2,500),ID(18)

#include "parcc.com"
      DATA ONEM,ONE,ZERO /-1.0D0,1.0D0,0.0D0/

      TOTSIZ=ISYMSZ(ISYTYP(1,23),ISYTYP(1,23))
      I000=1
      I010=I000+TOTSIZ*IINTFP
      I020=I010+TOTSIZ*IINTFP 
      IF(I020.GT.MAXCOR)CALL INSMEM('QUIKAA2',I020,MAXCOR)

      CALL GETALL(ICORE(I000),TOTSIZ,1,258)
      CALL GETALL(ICORE(I010),TOTSIZ,1,256)
      CALL DAXPY(TOTSIZ,ONE,ICORE(I000),1,ICORE(I010),1)
      CALL PUTALL(ICORE(I010),TOTSIZ,1,254)
      CALL DSCAL(TOTSIZ,ZERO,ICORE(I000),1)
      CALL PUTALL(ICORE(I000),TOTSIZ,1,258)

#ifdef _DEBUG_LVL0
      Write(6,*) 
      Write(6,"(a)") "@-PDCC_QUIKAA2"
      call getall(ICORE(I000),TOTSIZ,1,254)
      call checksum("List-254", ICORE(I000), TOTSIZ)
      call getall(ICORE(I000),TOTSIZ,1,256)
      call checksum("List-256", ICORE(I000), TOTSIZ)
      call getall(ICORE(I000),TOTSIZ,1,258)
      call checksum("List-258", ICORE(I000), TOTSIZ)
#endif 

      RETURN
      END
