      SUBROUTINE SYMETRIZE_T2(WORK, MAXCOR, IRREPX, T2AB_LIST)

      IMPLICIT INTEGER(A-Z)
      DOUBLEPRECISION WORK(MAXCOR)
      DOUBLE PRECISION HALF 
C
      COMMON/SYMINF/NSTART,NIRREP,IRREPY(255,2),DIRPRD(8,8)
      COMMON/SYM/POP(8,2),VRT(8,2),NT(2),NFMI(2),NFEA(2)
      COMMON/SYMPOP/IRPDPD(8,22),ISYTYP(2,500),ID(18)
C
      DATA HALF /0.50D0/ 

      DO IRREP_R = 1, NIRREP
         IRREP_L = DIRPRD(IRREP_R, IRREPX)
        
         NROWS = IRPDPD(IRREP_L, 13)
         NCOLS = IRPDPD(IRREP_R, 14)
         MAXOF2= MAX(NCOLS, NROWS)
         
         I000 = 1
         I010 = I000 + NROWS*NCOLS
         I020 = I010 + MAXOF2
         I030 = I020 + MAXOF2
         I040 = I030 + MAXOF2 
C
         IF (I040 .GT. MAXCOR) CALL INSMEM("@SYMETRIZE_T2", 
     &                         I040, MAXCOR)
C
         CALL GETLST(WORK(I000), 1, NCOLS, 1, IRREP_R, T2AB_LIST)
         CALL SYMRHF3(IRREP_L, IRREP_R, VRT, POP, NROWS, WORK(I000),
     &                WORK(I010), WORK(I020), WORK(I030))
         CALL DSCAL(NROWS*NCOLS, HALF, WORK(I000), 1)
         CALL PUTLST(WORK(I000), 1, NCOLS, 1, IRREP_R, T2AB_LIST)
      ENDDO
C
      RETURN
      END
 
       
