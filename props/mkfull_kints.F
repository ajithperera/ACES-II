      SUBROUTINE MKFULL_KINTS(WFULL,WCHUNK,SCR,LDIM1,LDIM2,NBAS,
     &                       JFLAG,NBFIRR,IRPOFF,ITRIOF,NIRREP)
C
      IMPLICIT DOUBLE PRECISION (A-H,O-Z)
C
      DIMENSION WFULL(NBAS,NBAS),WCHUNK(LDIM1)
      DIMENSION SCR(LDIM2)
      DIMENSION NBFIRR(8)
      DIMENSION ITRIOF(9)
      DIMENSION IRPOFF(9)
C
      INDX2(I,J,N)=I+(J-1)*N

      DO 101 I=1,NIRREP
         CALL EXPND2(WCHUNK(ITRIOF(I)),SCR,NBFIRR(I))
         DO 111 J=1,NBFIRR(I)
         DO 121 K=1,NBFIRR(I)
              WFULL(IRPOFF(I)+J,IRPOFF(I)+K)=SCR(INDX2(J,K,NBFIRR(I)))
  121       CONTINUE
  111     CONTINUE
  101   CONTINUE
C
      RETURN
      END
