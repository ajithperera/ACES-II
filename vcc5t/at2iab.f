      SUBROUTINE AT2IAB(ICORE,IND,INT2)
      IMPLICIT INTEGER (A-Z)
      DOUBLE PRECISION ICORE(1)
      LOGICAL INT2
C
      COMMON /MACHSP/ IINTLN,IFLTLN,IINTFP,IALONE,IBITWD
      COMMON /SYMINF/ NSTART,NIRREP,IRREPA(255),IRREPB(255),
     1                DIRPRD(8,8)
      COMMON /SYMPOP/ IRPDPD(8,22),ISYTYP(2,500),ID(18)
      COMMON /SYM/    POP(8,2),VRT(8,2),NTAA,NTBB,NF1AA,NF2AA,
     1                NF1BB,NF2BB
      COMMON /INFO/   NOCCO(2),NVRTO(2)
      COMMON /FLAGS/  IFLAGS(100)
      EQUIVALENCE(ICLLVL,IFLAGS( 2))
      EQUIVALENCE(IDRLVL,IFLAGS( 3))
      EQUIVALENCE(IREFNC,IFLAGS(11))
C
      COMMON /GAMLIS/ LISGO1,LISGO2,LISGO3,LISGO4,LISGV1,LISGV2,
     1                LISGV3,LISGV4
      COMMON /T2ILIS/ LIST2I1,LIST2I2,LIST2I3
C
      COMMON /T3OFF/  IOFFVV(8,8,10),IOFFOO(8,8,10),IOFFVO(8,8,4)
      COMMON /LISWI/  LWIC11,LWIC12,LWIC13,LWIC14,
     1                LWIC15,LWIC16,LWIC17,LWIC18,
     1                LWIC21,LWIC22,LWIC23,LWIC24,
     1                LWIC25,LWIC26,LWIC27,LWIC28,
     1                LWIC31,LWIC32,LWIC33,
     1                LWIC34,LWIC35,LWIC36,
     1                LWIC37,LWIC38,LWIC39,LWIC40,LWIC41,LWIC42
C
C     SUBROUTINE TO ALTER T2 AB INCREMENT LIST IN ONE OF TWO WAYS :
C
C     (1) If IND is 0 (Alpha,Beta) ---> (Beta,Alpha)
C     (2) Otherwise   (Beta,Alpha) ---> (Alpha,Beta)
C
      WRITE(6,1000)
 1000 FORMAT(' @AT2IAB-I, Some AbIj lists are being altered. ')
C
      IF(IND.EQ.0)THEN
C
      IF(INT2)THEN
      DO   10 IRPIJ = 1,NIRREP
      I000 = 1
      I010 = I000 + MAX0(IRPDPD(IRPIJ,13),IRPDPD(IRPIJ,14))
      I020 = I010 + MAX0(IRPDPD(IRPIJ,13),IRPDPD(IRPIJ,14))
      I030 = I020 + MAX0(IRPDPD(IRPIJ,13),IRPDPD(IRPIJ,14))
      CALL GETLST(ICORE(I030),1,IRPDPD(IRPIJ,14),1,IRPIJ,LIST2I3)
      CALL SYMTR3(IRPIJ,VRT(1,1),VRT(1,2),
     1            IRPDPD(IRPIJ,13),IRPDPD(IRPIJ,14),
     1            ICORE(I030),ICORE(I000),ICORE(I010),ICORE(I020))
      CALL PUTLST(ICORE(I030),1,IRPDPD(IRPIJ,14),2,IRPIJ,LIST2I3)
   10 CONTINUE
      ENDIF
C
      DO   20 IRPIJ = 1,NIRREP
      I000 = 1
      I010 = I000 + MAX0(IRPDPD(IRPIJ,13),IRPDPD(IRPIJ,14))
      I020 = I010 + MAX0(IRPDPD(IRPIJ,13),IRPDPD(IRPIJ,14))
      I030 = I020 + MAX0(IRPDPD(IRPIJ,13),IRPDPD(IRPIJ,14))
      CALL GETLST(ICORE(I030),1,IRPDPD(IRPIJ,14),1,IRPIJ,46)
      CALL SYMTR3(IRPIJ,VRT(1,1),VRT(1,2),
     1            IRPDPD(IRPIJ,13),IRPDPD(IRPIJ,14),
     1            ICORE(I030),ICORE(I000),ICORE(I010),ICORE(I020))
      CALL PUTLST(ICORE(I030),1,IRPDPD(IRPIJ,14),2,IRPIJ,46)
   20 CONTINUE
C
      DO   30 IRPIJ = 1,NIRREP
      I000 = 1
      I010 = I000 + MAX0(IRPDPD(IRPIJ,13),IRPDPD(IRPIJ,14))
      I020 = I010 + MAX0(IRPDPD(IRPIJ,13),IRPDPD(IRPIJ,14))
      I030 = I020 + MAX0(IRPDPD(IRPIJ,13),IRPDPD(IRPIJ,14))
      CALL GETLST(ICORE(I030),1,IRPDPD(IRPIJ,14),1,IRPIJ,16)
      CALL SYMTR3(IRPIJ,VRT(1,1),VRT(1,2),
     1            IRPDPD(IRPIJ,13),IRPDPD(IRPIJ,14),
     1            ICORE(I030),ICORE(I000),ICORE(I010),ICORE(I020))
      CALL PUTLST(ICORE(I030),1,IRPDPD(IRPIJ,14),2,IRPIJ,16)
   30 CONTINUE
C
      IF(IDRLVL.GT.0.AND.(ICLLVL.EQ.21.OR.ICLLVL.EQ.22))THEN
      DO   40 IRPIJ = 1,NIRREP
      I000 = 1
      I010 = I000 + MAX0(IRPDPD(IRPIJ,13),IRPDPD(IRPIJ,14))
      I020 = I010 + MAX0(IRPDPD(IRPIJ,13),IRPDPD(IRPIJ,14))
      I030 = I020 + MAX0(IRPDPD(IRPIJ,13),IRPDPD(IRPIJ,14))
      CALL GETLST(ICORE(I030),1,IRPDPD(IRPIJ,14),1,IRPIJ,116)
      CALL SYMTR3(IRPIJ,VRT(1,1),VRT(1,2),
     1            IRPDPD(IRPIJ,13),IRPDPD(IRPIJ,14),
     1            ICORE(I030),ICORE(I000),ICORE(I010),ICORE(I020))
      CALL PUTLST(ICORE(I030),1,IRPDPD(IRPIJ,14),2,IRPIJ,116)
   40 CONTINUE
      ENDIF
C
      ELSE
C
      IF(INT2)THEN
      DO  110 IRPIJ = 1,NIRREP
      I000 = 1
      I010 = I000 + MAX0(IRPDPD(IRPIJ,13),IRPDPD(IRPIJ,14))
      I020 = I010 + MAX0(IRPDPD(IRPIJ,13),IRPDPD(IRPIJ,14))
      I030 = I020 + MAX0(IRPDPD(IRPIJ,13),IRPDPD(IRPIJ,14))
      CALL GETLST(ICORE(I030),1,IRPDPD(IRPIJ,14),1,IRPIJ,LIST2I3)
      CALL SYMTR3(IRPIJ,VRT(1,2),VRT(1,1),
     1            IRPDPD(IRPIJ,13),IRPDPD(IRPIJ,14),
     1            ICORE(I030),ICORE(I000),ICORE(I010),ICORE(I020))
      CALL PUTLST(ICORE(I030),1,IRPDPD(IRPIJ,14),2,IRPIJ,LIST2I3)
  110 CONTINUE
      ENDIF
C
      DO  120 IRPIJ = 1,NIRREP
      I000 = 1
      I010 = I000 + MAX0(IRPDPD(IRPIJ,13),IRPDPD(IRPIJ,14))
      I020 = I010 + MAX0(IRPDPD(IRPIJ,13),IRPDPD(IRPIJ,14))
      I030 = I020 + MAX0(IRPDPD(IRPIJ,13),IRPDPD(IRPIJ,14))
      CALL GETLST(ICORE(I030),1,IRPDPD(IRPIJ,14),1,IRPIJ,46)
      CALL SYMTR3(IRPIJ,VRT(1,2),VRT(1,1),
     1            IRPDPD(IRPIJ,13),IRPDPD(IRPIJ,14),
     1            ICORE(I030),ICORE(I000),ICORE(I010),ICORE(I020))
      CALL PUTLST(ICORE(I030),1,IRPDPD(IRPIJ,14),2,IRPIJ,46)
  120 CONTINUE
C
      DO  130 IRPIJ = 1,NIRREP
      I000 = 1
      I010 = I000 + MAX0(IRPDPD(IRPIJ,13),IRPDPD(IRPIJ,14))
      I020 = I010 + MAX0(IRPDPD(IRPIJ,13),IRPDPD(IRPIJ,14))
      I030 = I020 + MAX0(IRPDPD(IRPIJ,13),IRPDPD(IRPIJ,14))
      CALL GETLST(ICORE(I030),1,IRPDPD(IRPIJ,14),1,IRPIJ,16)
      CALL SYMTR3(IRPIJ,VRT(1,2),VRT(1,1),
     1            IRPDPD(IRPIJ,13),IRPDPD(IRPIJ,14),
     1            ICORE(I030),ICORE(I000),ICORE(I010),ICORE(I020))
      CALL PUTLST(ICORE(I030),1,IRPDPD(IRPIJ,14),2,IRPIJ,16)
  130 CONTINUE
C
      IF(IDRLVL.GT.0.AND.(ICLLVL.EQ.21.OR.ICLLVL.EQ.22))THEN
      DO  140 IRPIJ = 1,NIRREP
      I000 = 1
      I010 = I000 + MAX0(IRPDPD(IRPIJ,13),IRPDPD(IRPIJ,14))
      I020 = I010 + MAX0(IRPDPD(IRPIJ,13),IRPDPD(IRPIJ,14))
      I030 = I020 + MAX0(IRPDPD(IRPIJ,13),IRPDPD(IRPIJ,14))
      CALL GETLST(ICORE(I030),1,IRPDPD(IRPIJ,14),1,IRPIJ,116)
      CALL SYMTR3(IRPIJ,VRT(1,2),VRT(1,1),
     1            IRPDPD(IRPIJ,13),IRPDPD(IRPIJ,14),
     1            ICORE(I030),ICORE(I000),ICORE(I010),ICORE(I020))
      CALL PUTLST(ICORE(I030),1,IRPDPD(IRPIJ,14),2,IRPIJ,116)
  140 CONTINUE
      ENDIF
C
      ENDIF
C
      RETURN
      END
