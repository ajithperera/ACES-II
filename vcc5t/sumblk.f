      SUBROUTINE SUMBLK(U,LEN)
      IMPLICIT DOUBLE PRECISION (A-H,O-Z)
      DIMENSION U(LEN)
      CHKSUM = 0.0D+00
      DO   10 I=1,LEN
C     WRITE(6,*) ' @SUMBLK-I, I,U(I),LEN ',I,U(I),LEN
      CHKSUM = CHKSUM + DABS(U(I))
   10 CONTINUE
      WRITE(6,*) ' @SUMBLK-I, CHKSUM ',CHKSUM
      RETURN
      END
