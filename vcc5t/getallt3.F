      SUBROUTINE GETALLT3
      IMPLICIT DOUBLE PRECISION (A-H,O-Z)
      INTEGER DISTSZ
#include "trps.par"
      DIMENSION BUF(4096)
      COMMON /MACHSP/ IINTLN,IFLTLN,IINTFP,IALONE,IBITWD
      COMMON /T3FILE/ ALLT3(NUMT3)
      COMMON /AUXIO/ DISTSZ(8,100),NDISTS(8,100),INIWRD(8,100),LNPHYR,
     1               NRECS,LUAUX
C
      WRITE(6,1000)
 1000 FORMAT(' @GETALLT3-I, Reading all of AUXMOI ! ')
      CALL ZERO(ALLT3,NUMT3)
      DO   10 IREC=1,NRECS
      READ(LUAUX,REC=IREC) BUF
c YAU : old
c     CALL ICOPY(LNPHYR*IINTFP,BUF,1,ALLT3(1+(IREC-1)*LNPHYR),1)
c YAU : new
      CALL DCOPY(LNPHYR,BUF,1,ALLT3(1+(IREC-1)*LNPHYR),1)
c YAU : end
   10 CONTINUE
      RETURN
      END
