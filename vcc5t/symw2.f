      SUBROUTINE SYMW2(W,W2,NROW1,NCOL1,NROW2,NCOL2,ISPIN1,ISPIN2,
     1                 IOFF1,IOFF2,IRPA,IRPC,IRPD)
      IMPLICIT DOUBLE PRECISION (A-H,O-Z)
      INTEGER A,C,D,POP,VRT
      DIMENSION W2(NROW2,NCOL2),W(NROW1,NCOL1)
      COMMON /SYM/    POP(8,2),VRT(8,2),NTAA,NTBB,NF1AA,NF2AA,
     1                NF1BB,NF2BB
C
      DO 30     D=1,VRT(IRPD,ISPIN2)
      DO 20     C=1,VRT(IRPC,ISPIN1)
      DO 10     A=1,VRT(IRPA,ISPIN1)
C
        W(IOFF1 + (D-1)*VRT(IRPC,ISPIN1) + C,A) =
     1 W2(IOFF2 + (C-1)*VRT(IRPA,ISPIN1) + A,D)
C
   10 CONTINUE
   20 CONTINUE
   30 CONTINUE
      RETURN
      END
