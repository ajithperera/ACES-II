      SUBROUTINE PSI4DBG_FORMX(ICORE,MAXCOR,IUHF)
C
      IMPLICIT DOUBLE PRECISION(A-H,O-Z)
      INTEGER POP,VRT,DIRPRD
      LOGICAL PCCD,CCD,LCCD
      DIMENSION ICORE(MAXCOR)
C
      COMMON /MACHSP/ IINTLN,IFLTLN,IINTFP,IALONE,IBITWD
      COMMON/SYM/POP(8,2),VRT(8,2),NT(2),NF1(2),NF2(2)
      COMMON/SYMINF/NSTART,NIRREP,IRREPS(255,2),DIRPRD(8,8)
      COMMON/SYMPOP/IRPDPD(8,22),ISYTYP(2,500),ID(18)
      COMMON/METH/pCCD,CCD,LCCD,MBPT2
C
      CALL PSI4DBG_FORMH4(ICORE,MAXCOR,IUHF)
      IF (.NOT. LCCD) CALL PSI4DBG_V1INX2(ICORE,MAXCOR,IUHF)
      call checkgamma1(icore,maxcor,iuhf)
      IF (.NOT. LCCD) CALL PSI4DBG_H4INX2(ICORE,MAXCOR,IUHF)
      call checkgamma1(icore,maxcor,iuhf)
      IF (.NOT. LCCD) CALL PSI4DBG_G1INX2(ICORE,MAXCOR,IUHF)
      call checkgamma1(icore,maxcor,iuhf)
      IF (.NOT. LCCD) CALL PSI4DBG_G2INX2(ICORE,MAXCOR,IUHF)
      call checkgamma1(icore,maxcor,iuhf)

      RETURN
      END
