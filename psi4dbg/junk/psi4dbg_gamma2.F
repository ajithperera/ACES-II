       SUBROUTINE PSI4DBG_GAMMA2(ICORE,MAXCOR,IUHF,LISTOFF)
C
      IMPLICIT DOUBLE PRECISION (A-H,O-Z)
      INTEGER DIRPRD,DISSYT,POP,VRT

      DIMENSION ICORE(MAXCOR)
C
      COMMON/MACHSP/IINTLN,IFLTLN,IINTFP,IALONE,IBITWD
      COMMON/SYMINF/NSTART,NIRREP,IRREPA(255,2),DIRPRD(8,8)
      COMMON/SYM/POP(8,2),VRT(8,2),NTAA,NTBB,NF1AA,NF1BB,NF2AA,NF2BB
      COMMON/SYMPOP/IRPDPD(8,22),ISYTYP(2,500),NTOT(18)
C
      DATA ONE /1.0D+0/

      LISTG=133+LISTOFF
      LISTT1=46
      LISTT2=46
      FACT=ONE

      DO 110 IRREP=1,NIRREP
C
        DISSYT=IRPDPD(IRREP,ISYTYP(1,LISTT1))
        NUMSYT=IRPDPD(IRREP,ISYTYP(2,LISTT1))
        I001=1
        I002=I001+IINTFP*NUMSYT*DISSYT
        I003=I002+IINTFP*NUMSYT*DISSYT
        MAXSIZE=(MAXCOR-I003)/IINTFP
        IF(MAXSIZE.GT.DISSYT) THEN

        CALL PCCD_G2ALL(ICORE(I001),ICORE(I002),ICORE(I003),MAXSIZE,
     &                  FACT,3,DISSYT,NUMSYT,LISTT1,LISTT2,LISTG,IRREP)
       ELSE
          CALL INSMEM('PCCD_GAMMA2',DISSYT,MAXSIZE)
       ENDIF
110    CONTINUE

#ifdef _DEBUG_LVL0
      TWO=1.D0
      CALL CHECKGAM1(ICORE,233,133,TWO,IUHF,2,VRT)
#endif 
C
      RETURN
      END
