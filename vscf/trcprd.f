
      DOUBLE PRECISION FUNCTION TRCPRD(A,B,NRA,NCA)
C
C THIS ROUTINE RETURNS THE TRACE OF THE MATRIX C WHICH IS
C   GIVEN BY THE PRODUCT OF A AND B (C=A*B).  C IS A SQUARE
C   MATRIX OF DIMENSION NRA x NRA AND A AND B ARE MATRICES OF
C   DIMENSION A(NRA,NCA) AND B(NCA,NRA) RESPECTIVELY.
C
CEND
      IMPLICIT DOUBLE PRECISION (A-H,O-Z)
      DIMENSION A(NRA,NCA),B(NCA,NRA)
      TRCPRD=0.
      DO 10 I=1,NRA
       TRCPRD=TRCPRD+SDOT(NCA,B(1,I),1,A(I,1),NCA)
10    CONTINUE
      RETURN
      END
