      SUBROUTINE CSHPRI(CMAT,NCOORD,NUCIND,MULT,NAMEX,IFLAG)
C
C PRINT ROUTINE FOR SHIELDING TENSORS
C
CEND
C
C SEPTEMBER/91   JG  UNIVERSITY OF KARLSRUHE
C
      IMPLICIT DOUBLE PRECISION (A-H,O-Z)
C
      INTEGER DIRPRD
      CHARACTER NAMEX*6
C
      DIMENSION CMAT(3,NCOORD),NAMEX(1),MULT(1)
C
      DATA AZERO,THREE /0.D0,3.D0/
C
      
      WRITE (6,1000)
1000  FORMAT(24X,'Bx',10X,'By',10X,'Bz')
      WRITE (6,1001)
1001  FORMAT(1X)
      I=0
      DO 200 IATOM = 1, NUCIND
       MULTI=MULT(IATOM)
       ISTR=3*IATOM-3
       IF(MULTI.EQ.1) THEN
       DO 300 ICOOR = 1, 3
        I=I+1
        WRITE(6,1002)' ',NAMEX(ISTR+ICOOR)(1:4),' ',
     &       NAMEX(ISTR+ICOOR)(6:6),(CMAT(IFIELD,I),IFIELD=1,3)
1002    FORMAT(5X,A1,A4,A1,3X,A1,3F12.6)
300    CONTINUE
       WRITE(6,1001)
       ELSE
       DO 400 J=1,MULTI 
        DO 500 ICOOR=1,3
        I=I+1
        WRITE(6,1003)' ',NAMEX(ISTR+ICOOR)(1:4),J,
     &       NAMEX(ISTR+ICOOR)(6:6),(CMAT(IFIELD,I),IFIELD=1,3)
1003    FORMAT(5X,A1,A4,I1,3X,A1,3F12.6)
500     CONTINUE
        WRITE(6,1001)
400    CONTINUE
       ENDIF
200   CONTINUE   
      RETURN
      END
