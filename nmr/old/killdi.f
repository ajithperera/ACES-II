      SUBROUTINE KILLDI
C
C THIS ROUTINE DISPENSES WITH THE DERINT FILE AND ZEROS OUT ALL OF
C THE ASSOCIATED LIST POINTERS. IN ADDITION IT DELETES AS WELL
C THE DERGAM FILE
C
CEND
      IMPLICIT INTEGER(A-Z)
      LOGICAL YESNO
      CHARACTER*80 FNAME
C
      COMMON /LISTS/ MOIO(10,500),MOIOWD(10,500),MOIOSZ(10,500),
     &              MOIODS(10,500),MOIOFL(10,500)
      COMMON /CACHEINF/ CACHNUM,CACHNMP1,CACHDIR(100),CACHPOS(100),
     &                  CACHFILE(100),CACHMOD(100),OLDEST
C
      CALL GFNAME('DERINT  ',FNAME,ILENGTH)
      INQUIRE(FILE=FNAME(1:ILENGTH),EXIST=YESNO)
C
      IF(YESNO) THEN
       CLOSE(UNIT=53,STATUS='DELETE')
       CALL IZERO(MOIO(1,301),1000)
       CALL IZERO(MOIOWD(1,301),1000)
       CALL IZERO(MOIODS(1,301),1000)
       CALL IZERO(MOIOSZ(1,301),1000)
       CALL IZERO(MOIOFL(1,301),1000)
      ENDIF
C
      CALL GFNAME('DERGAM  ',FNAME,ILENGTH)
      INQUIRE(FILE=FNAME(1:ILENGTH),EXIST=YESNO)
C
      IF(YESNO) THEN
       CLOSE(UNIT=54,STATUS='DELETE')
       CALL IZERO(MOIO(1,401),1000)
       CALL IZERO(MOIOWD(1,401),1000)
       CALL IZERO(MOIODS(1,401),1000)
       CALL IZERO(MOIOSZ(1,401),1000)
       CALL IZERO(MOIOFL(1,401),1000)
      ENDIF
C
C ZERO THE CORRESPONDING CACHES KEEPING RECORDS OF UNIT 53 AND 54
C
      DO 10 I=1,100
       IF(CACHFILE(I).EQ.53)CACHFILE(I)=0
       IF(CACHFILE(I).EQ.54)CACHFILE(I)=0
10    CONTINUE
C
      RETURN
      END
