
      SUBROUTINE TRACSH(SCSH,CCSH,TRSTOC,NCOOR)
      IMPLICIT DOUBLE PRECISION (A-H,O-Z)
      PARAMETER (D1 = 1.0D0)
      DIMENSION SCSH(3,1),CCSH(3,1),TRSTOC(NCOOR,NCOOR)
C
      COMMON/MACHSP/IINTLN,IFLTLN,IINTFP,IALONE,IBITWD
      COMMON/PERT/NTPERT,NPERT(8),IPERT(8),IXYZ(3),IJUNK(7)
C
      DATA AZERO,ONE /0.0D0,1.D0/
C
      CALL GETREC(20,'JOBARC','SYMCOORD',IINTFP*NCOOR*NCOOR,
     &            TRSTOC)
C
      CALL XGEMM('N','T',3,NCOOR,NCOOR,ONE,SCSH,3,
     &           TRSTOC,NCOOR,AZERO,CCSH,3)
C
      RETURN
      END
