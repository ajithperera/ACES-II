      SUBROUTINE READF(FIJA,FIJB,FABA,FABB,FAIA,FAIB,IUHF)
C
C  THIS SUBROUTINE READS IN ALL FOCK-MATRICES REQUIRED IN
C  THE CASE OF ROHF OR QRHF REFERENCE FUNCTIONS.
C
CEND
      IMPLICIT DOUBLE PRECISION(A-H,O-Z)
      DIMENSION FIJA(1),FIJB(1),FABA(1),FABB(1),FAIA(1),FAIB(1)
C
      CALL GETLST(FIJA,1,1,1,3,91) 
      CALL GETLST(FIJB,1,1,1,4,91) 
      CALL GETLST(FABA,1,1,1,3,92) 
      CALL GETLST(FABB,1,1,1,4,92) 
      CALL GETLST(FAIA,1,1,1,3,93) 
      CALL GETLST(FAIB,1,1,1,4,93) 
C
      RETURN
      END
