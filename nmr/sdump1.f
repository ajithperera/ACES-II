      SUBROUTINE SDUMP1(AODINT,BUF,BUF1,NSTART,NEND,IRREPX)
C
C THIS ROUTINE DUMPS THE INTEGRALS <AB|CI(chi)> TO DERINT
C SYMMETRY CASE IIII AND IIIJ
C
CEND
      IMPLICIT DOUBLE PRECISION(A-H,O-Z)
      INTEGER DIRPRD,AOPOP,POP,VRT
C
      DIMENSION AODINT(1),BUF(1),BUF1(1)
      DIMENSION IOFFVV(8),NSTART(8),NEND(8)
C
      COMMON/MACHSP/IINTLN,IFLTLN,IINTFP,IALONE,IBITWD
      COMMON/SYMINF/NDUMMY,NIRREP,IRREPA(255,2),DIRPRD(8,8)
      COMMON/SYMPOP/IRPDPD(8,22),ISYTYP(2,500),ID(18)
      COMMON/SYM/POP(8,2),VRT(8,2),NT(2),NF1(2),NF2(2)
      COMMON/AOPOPS/AOPOP(8)
C
      INDXF(I,J,N)=I+N*(J-1)
C
C INITIALIZE OFFSETS
C
      IOFF=1
      IOFFVO=0
      IOFFV=0
      DO 1 IRREP=1,NIRREP
       IOFFVV(IRREP)=IOFFV
       IOFFV=IOFFV+VRT(IRREP,1)*VRT(IRREP,1)
1     CONTINUE
C
C LOOP OVER ALL IRREPS
C
      DO 1000 IRREPR=1,NIRREP
C
       IRREPL=DIRPRD(IRREPR,IRREPX)
       NOCC=POP(IRREPR,1)
       NSTARTI=NSTART(IRREPR)
       NENDI=NEND(IRREPR)
       NVRT=VRT(IRREPL,1)
       NVRT2F=NVRT*NVRT
       NVRT2=NVRT*(NVRT+1)/2
       NVRT3=NVRT2*NVRT 
C
       DO 100 IND=NSTARTI,NENDI
C
C REORDER ADOINT ARRAY, BUT ONLY INTEGRALS WHICH BELONG TO
C IRREP (THIS IS DONE IN EXPND5)
C
        CALL SCOPY(NVRT3,AODINT(IOFF),1,BUF,1)
        IOFF=IOFF+NVRT3
        CALL EXPND5(BUF,NVRT,NVRT,BUF1,0)
C
        LENGTH=NVRT*NVRT
        IF(LENGTH.NE.0) THEN
         DO 65 IQ=1,NVRT
          IOFFABCI=INDXF(1,IQ,NVRT2F)
          IDISABCI=IOFFVO+INDXF(IQ,IND,NVRT)
          LOFFABCI=IOFFVV(IRREPL)
          CALL ULIST(BUF(IOFFABCI),BUF1,LENGTH,IDISABCI,LOFFABCI,
     &               1,1,IRREPX,330,2)
65       CONTINUE
        ENDIF
C
C UPDATE OFFSETS
C
100    CONTINUE
C
       IOFFVO=IOFFVO+NVRT*NOCC
C
1000  CONTINUE
C
      RETURN
      END
