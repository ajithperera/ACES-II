      SUBROUTINE GETTRN3(Z,BUF,NDSSIZ,ISTART,IEND,NUMDIS,
     &                   ICACHE,ISPIN,ITYPE)
C
C THIS ROUTINE RETRIEVES THE TRANSPOSE OF A LIST ON DISK. THIS
C ROUTINE READS ONLY DISTRIBUTION ISTART TO IEND
C
C  INPUT:
C         NDSSIZ - ACTUAL DISTRIBUTION SIZE OF LIST ON DISK.
C         ISTART - FIRST DISTRIBUTION TO READ
C         IEND   - LAST DISTRIBUTION TO READ
C         NUMDIS - IEND+1-ISTART
C         ICACHE - I/O CACHE TO BE USED.
C         ISPIN - SPIN OR IRREP SPECIFIER FOR LIST.
C         ITYPE - LIST NUMBER.
C
C  SCRATCH:
C            BUF - INTERMEDIATE ARRAY HOLDING DISTRIBUTIONS (SIZE: NDSSIZ)
C
C  OUTPUT:
C              Z - THE TRANSPOSED LIST, TREATED AS A MATRIX WITH
C                   DIMENSIONS (NUMDIS,NDSSIZ).
C     
CEND
      IMPLICIT INTEGER (A-Z)
      DOUBLE PRECISION Z(NUMDIS,NDSSIZ),BUF(NDSSIZ)
      CALL ZERO(Z,NUMDIS*NDSSIZ)
      DO 10 I=ISTART,IEND
       CALL GETLST(BUF,I,1,ICACHE,ISPIN,ITYPE)
       CALL SCOPY(NDSSIZ,BUF,1,Z(I-ISTART+1,1),NUMDIS)
10    CONTINUE
      RETURN
      END
