      SUBROUTINE DINTVO2(DIVO,DDOO,DDVV,ICORE,MAXCOR,IUHF,IP,ANTI)
C
CEND
C
C CODED JUNE/91 JG
C
      IMPLICIT DOUBLE PRECISION(A-H,O-Z)
      LOGICAL MBPT2,MBPT3,M4DQ,M4SDQ,M4SDTQ,CCD,QCISD,CCSD,ANTI
C
      DIMENSION ICORE(MAXCOR),DIVO(1),DDOO(1),DDVV(1)
C
      COMMON/METH/MBPT2,MBPT3,M4DQ,M4SDQ,M4SDTQ,CCD,QCISD,CCSD
      COMMON /TIMEINFO/ TIMEIN, TIMENOW, TIMETOT, TIMENEW 
C
      CALL TIMER(1)
C 
      CALL DIVO21(DIVO,DDOO,ICORE,MAXCOR,IUHF,IP,ANTI)
      CALL DIVO22(DIVO,DDVV,ICORE,MAXCOR,IUHF,IP,ANTI)
C
C CALCULATE FIRST CONTRIBUTIONS TO DI(A,I)
C
      CALL DIVO23(DIVO,ICORE,MAXCOR,IUHF,IP,ANTI)
C
C FOR HIGHER ORDER PERTURBATION THEORY AND CC METHODS, ADDITIONAL TERMS
C HAVE TO BE CONSIDERED
C
      IF(.NOT.MBPT2) THEN
C
c       CALL DIVO24(DIVO,ICORE,MAXCOR,IUHF,IP,ANTI)
c       CALL DIVO25(DIVO,ICORE,MAXCOR,IUHF,IP,ANTI)
C
C ADDITIONAL TERMS FOR FOURTH ORDER AND QCISD AND CCSD
C
       IF(.NOT.MBPT3.AND..NOT.CCD) THEN
C
c        CALL DIVO26(DIVO,ICORE,MAXCOR,IUHF,IP,ANTI)
c        CALL DIVO27(DIVO,ICORE,MAXCOR,IUHF,IP,ANTI)
c        CALL DIVO28(DIVO,ICORE,MAXCOR,IUHF,IP,ANTI)
C
       ENDIF
      ENDIF
C
C ALL DONE
C
      CALL TIMER(1)
      write(6,6001) TIMENEW
6001  FORMAT(' Calculation of the contributions of <pq||rs> to',
     &       ' d I(a,i)/ d chi',/,' required ',f5.1,' seconds.')
      RETURN
C
      END 
