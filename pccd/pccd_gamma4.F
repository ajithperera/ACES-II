      SUBROUTINE PCCD_GAMMA4(ICORE,MAXCOR,IUHF)
      IMPLICIT DOUBLE PRECISION(A-H,O-Z)
      DIMENSION ICORE(MAXCOR)
C
      DATA ONE/1.D0/
C
      CALL PCCD_G4ALL(ICORE,MAXCOR,'ABBA',IUHF)
      CALL PCCD_G4ALL(ICORE,MAXCOR,'ABAB',IUHF)

      IF(IUHF.EQ.0) THEN
         CALL QUIKAA(ICORE,MAXCOR)
      ENDIF
C
#ifdef _DEBUG_LVL0
      TWO=2.0D0/DFLOAT(1+IUHF)
      CALL CHECKGAM(ICORE,23,123,TWO)
      CALL CHECKGAM(ICORE,25,125,TWO)
      CALL CHECKGAM(ICORE,18,118,TWO)
#endif 
C
      RETURN
      END
