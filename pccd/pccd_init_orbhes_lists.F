      SUBROUTINE PCCD_INIT_ORBHES_LISTS(ICORE,MAXCOR,IUHF)
C
      IMPLICIT INTEGER(A-Z)
      DIMENSION ICORE(MAXCOR)
C
      IMODE=0

C Note that 233 is reserved for W(ABCD) or Hbar(ABCD)

      CALL INIPCK(1,15,15,234,IMODE,0,1)

C Intermediate PPPP list

      CALL INIPCK(1,15,15,235,IMODE,0,1)

C HHHH lists
      CALL INIPCK(1,14,14,213,IMODE,0,1)

C Intermediate HHHH list

      CALL INIPCK(1,14,14,214,IMODE,0,1)

C PHPH/HPPH lists

      CALL INIPCK(1,9,9,223,IMODE,0,1)
      CALL INIPCK(1,18,11,218,IMODE,0,1)
      CALL INIPCK(1,11,11,225,IMODE,0,1)

C PPHH and HHPP lists 

      CALL INIPCK(1,15,14,216,IMODE,0,1)
      CALL INIPCK(1,14,15,217,IMODE,0,1)

C Intermediate PPHH and HHPPlists
      CALL INIPCK(1,15,14,226,IMODE,0,1)
      CALL INIPCK(1,14,15,227,IMODE,0,1)

C HHHP and HPHH lists

      CALL INIPCK(1,14,18,210,IMODE,0,1)
      CALL INIPCK(1,18,14,211,IMODE,0,1)

C PPPH and PHPP lists

      CALL INIPCK(1,15,11,230,IMODE,0,1)
      CALL INIPCK(1,11,15,231,IMODE,0,1)

      RETURN
      END
