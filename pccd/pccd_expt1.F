      SUBROUTINE PCCD_EXPT1(T1PACK,T1FULL,NOCC,NVRT,ISPIN)
      IMPLICIT DOUBLE PRECISION (A-H,O-Z)
      INTEGER POP,VRT,DIRPRD
      DIMENSION T1PACK(1),T1FULL(NVRT,NOCC)
      COMMON /INFO/ NOCCO(2),NVRTO(2)
      COMMON /MACHSP/ IINTLN,IFLTLN,IINTFP,IALONE,IBITWD
      COMMON /SYM/ POP(8,2),VRT(8,2),NT(2),NF(4)
      COMMON /FLAGS/ IFLAGS(100)
      COMMON /SYMINF/NSTART,NIRREP,IRREPS(255,2),DIRPRD(8,8)
      CALL ZERO(T1FULL,NVRT*NOCC)
      LENGTH=NOCCO(ISPIN)*NVRTO(ISPIN)
      IOFFO=1
      IOFFV=1
      IOFF0=1
      DO 10 IRREP=1,NIRREP
       NOCIRR=POP(IRREP,ISPIN)
       NVRIRR=VRT(IRREP,ISPIN)
       DO 20 IOCC=1,NOCIRR
        CALL SCOPY(NVRIRR,T1PACK(IOFF0),1,T1FULL(IOFFV,IOFFO),1)
        IOFFO=IOFFO+1
        IOFF0=IOFF0+NVRIRR
20     CONTINUE
       IOFFV=IOFFV+NVRIRR
10    CONTINUE
      CALL SCOPY(LENGTH,T1FULL,1,T1PACK,1)
      RETURN
      END 
