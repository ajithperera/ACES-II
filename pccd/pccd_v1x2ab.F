      SUBROUTINE PCCD_V1X2AB(W,T,Z,DISSYW,NUMSYW,DISSYT,NUMSYT,
     &                       LISTW,LISTT,IRREP,IUHF)
C
      IMPLICIT DOUBLE PRECISION (A-H,O-Z)
      INTEGER DISSYT,DISSYW,DIRPRD
      CHARACTER*4 MODIF
      DIMENSION W(DISSYW,NUMSYW), T(DISSYT,NUMSYT)
      DIMENSION Z(DISSYT,NUMSYT)
      COMMON /MACHSP/IINTLN,IFLTLN,IINTFP,IALONE,IBITWD
      COMMON /SYMINF/ NSTART,NIRREP,IRREPA(255),IRREPB(255),
     &DIRPRD(8,8)
      COMMON/SYMPOP/IRPDPD(8,22),ISYTYP(2,500),NTOT(18)
      DATA ONE,AZERO,TWO /1.0D0,0.D0,2.0D0/

      CALL GETLST(T,1,NUMSYT,1,IRREP,LISTT)
      CALL GETLST(W,1,NUMSYW,2,IRREP,LISTW)
      CALL XGEMM('N','T',DISSYT,DISSYW,NUMSYT,ONE,T,
     &           DISSYT,W,DISSYW,AZERO,Z,DISSYT)

      CALL PUTLST(Z,1,NUMSYT,1,IRREP,116)

      RETURN
      END
