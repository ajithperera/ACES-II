      SUBROUTINE PCCD_L2INL2U(ICORE,MAXCOR,IUHF,IDOPPL)

      IMPLICIT DOUBLE PRECISION (A-H,O-Z)
C
C THIS ROUTINE CALCULATES THE L2 CONTRIBUTION TO THE L2 INCREMENT
C
      DIMENSION ICORE(MAXCOR)
      COMMON /FILES/ LUOUT,MOINTS
      COMMON /FLAGS/ IFLAGS(100)
      COMMON /FLAGS2/ iFlags2(500)
      COMMON /TIMEINFO/ TIMEIN, TIMENOW, TIMETOT, TIMENEW
C
      IF(IUHF.EQ.1)IBOT=1
      IF(IUHF.EQ.0)IBOT=3

      ITOP=6
      IF(IDOPPL.NE.0)ITOP=1
       DO 10 ITYPE=1,ITOP,5
        IF(ITYPE.EQ.6.AND.IFLAGS(93).EQ.2)THEN
         CALL PCCD_DRAOLAD(ICORE,MAXCOR,IUHF,.TRUE.,1,0,143,60,
     &                     243,260)
        ELSE
         CALL PCCD_L2LADU(ICORE,MAXCOR,IUHF,ITYPE)
        ENDIF
10     CONTINUE

#ifdef _DEBUG_LVL0
      Write(6,*) "The lambda residuals after l2lads"
      call check_leom(Icore,Maxcor,Iuhf)
#endif
     
         DO 15 ISPIN=IBOT,3
          CALL L2RNG(ICORE,MAXCOR,ISPIN,ITYPE,IUHF)
15       CONTINUE

      RETURN
      END
