      SUBROUTINE PCCD_FORMWL(ICORE,MAXCOR,IUHF)

      IMPLICIT INTEGER (A-Z)
      DOUBLE PRECISION ONE,TWO,ONEM
      DIMENSION ICORE(MAXCOR)
      COMMON /MACHSP/ IINTLN,IFLTLN,IINTFP,IALONE,IBITWD
      COMMON /SYMINF/ NSTART,NIRREP,IRREPA(255),IRREPB(255),DIRPRD(8,8)
      COMMON /SYMPOP/ IRPDPD(8,22),ISYTYP(2,500),ID(18)
      COMMON /FLAGS2/IFLAGS2(500)
      COMMON /FLAGS/IFLAGS(100)
      DATA ONE /1.0/
      DATA TWO /2.0/
      DATA ONEM /-1.0/

      ISIZE=ISYMSZ(ISYTYP(1,56),ISYTYP(2,56))
      I000=1
      I010=I000+IINTFP*ISIZE
      I020=I010+IINTFP*ISIZE
      I030=I020+IINTFP*ISIZE
      IF (I030.GT.MAXCOR+1) CALL INSMEM("FORMWL",(I020-1),MAXCOR)
      CALL GETALL(ICORE,ISIZE,1,56)
      CALL DSCAL(ISIZE,TWO,ICORE,1)
      CALL GETALL(ICORE(I010),ISIZE,1,18) 
      CALL GETALL(ICORE(I020),ISIZE,1,25) 
      CALL DSCAL(ISIZE,TWO,ICORE(I010),1)
      CALL DAXPY(ISIZE,ONEM,ICORE(I020),1,ICORE(I010),1)

      CALL DAXPY(ISIZE,ONEM,ICORE(I010),1,ICORE(I000),1)
      CALL PUTALL(ICORE,ISIZE,1,56)
      call checksum("List-56 :",ICORE,ISIZE)
C
      ISIZE=ISYMSZ(ISYTYP(1,58),ISYTYP(2,58))
      I000=1
      I010=I000+IINTFP*ISIZE
      I020=I010+IINTFP*ISIZE
      IF (I020.GT.MAXCOR+1) CALL INSMEM("FORMWL",(I020-1),MAXCOR)
      CALL GETALL(ICORE,ISIZE,1,58)
      CALL DSCAL(ISIZE,TWO,ICORE,1)
      CALL GETALL(ICORE(I010),ISIZE,1,25)
      CALL DAXPY(ISIZE,ONEM,ICORE(I010),1,ICORE(I000),1)
      CALL PUTALL(ICORE,ISIZE,1,58)
      call checksum("List-58 :",ICORE,ISIZE)

      RETURN
      END
