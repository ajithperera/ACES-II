      SUBROUTINE PCCD_AMPSUM(ICORE,MAXCOR,IUHF,NLIST,SING1,TYPE)
C
      IMPLICIT DOUBLE PRECISION (A-H,O-Z)
      LOGICAL SING1
      CHARACTER*1 TYPE
      DIMENSION ICORE(MAXCOR)
      COMMON /FLAGS /IFLAGS(100)

      IF(IUHF.NE.0)THEN
       I=3
       IF(SING1)CALL SORTT1(ICORE,MAXCOR,I,90+NLIST,IFLAGS(14),TYPE)
       CALL SORTT2(ICORE,MAXCOR,I,43+I+NLIST,IFLAGS(14),TYPE)
      ELSE
       CALL SORTT1(ICORE,MAXCOR,1,90+NLIST,IFLAGS(14),TYPE)
       CALL SORTT2(ICORE,MAXCOR,3,46+NLIST,IFLAGS(14),TYPE)
      ENDIF

      RETURN
      END
