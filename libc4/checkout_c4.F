      SUBROUTINE CHECKOUT_C4
      IMPLICIT INTEGER (A-Z)
      DOUBLE PRECISION TIMEIN,TIMEOUT,TIMDUM,TOTTIM
      COMMON /TIMINF/ TIMEIN
      CALL TIMER(TIMEOUT,TIMDUM,1)
      TOTTIM=TIMEOUT-TIMEIN
      WRITE(6,100)TOTTIM
100   FORMAT(T3,'@CHECKOUT-I, Total execution time : ',F12.4,
     &       ' seconds.')
      RETURN
      END
