      INTEGER FUNCTION ISRCHEQ_C4(N,ARRAY,INC,TARGET)
      IMPLICIT DOUBLE PRECISION (A-H,O-Z)
      INTEGER ARRAY,TARGET
      DIMENSION ARRAY(N)
      J=1
      IF(INC.LT.0)THEN
       JLOOK=1-(N-1)*INC
       INCR=-INC
      ELSE
       JLOOK=1
       INCR=INC
      ENDIF 
      DO 100 I=1,N
       IF(ARRAY(JLOOK).EQ.TARGET)GOTO 200
       JLOOK=JLOOK+INC
       J=J+INCR
100   CONTINUE
200   ISRCHEQ_C4=J
      RETURN
      END
