      SUBROUTINE INITIO_C4(INTIME,I0)
      IMPLICIT INTEGER (A-Z)
      LOGICAL YESNO,ISOPN,GAMLAM
      CHARACTER*8 NAMES(5)
      CHARACTER*80 FNAME
      INTEGER USEC
      COMMON / / ICORE(1)
      COMMON /INCORE/ ICREC(2),USEC,IXT(2),IMOD(2)
      COMMON /MACHSP/ IINTLN,IFLTLN,IINTFP,IALONE,IBITWD
      COMMON /FILES/ LUOUT,MOINTS
      COMMON /STARTS/ TBLOCK,WBLOCK
      COMMON /FSTRIPE/ NUMSPLIT(5)
      COMMON /LISTS/ MOIO(10,500),MOIOWD(10,500),MOIODS(10,500),
     &               MOIOSZ(10,500),MOIOFL(10,500)
      COMMON /FLAGS/ IFLAGS(100)
      COMMON /IOPOS/ ICRSIZ,ICHCSZ,IOFF(2),LENREC
      COMMON /FILSPC/ IBUFLN,IPRCLN,IPRCWD
      COMMON /CACHEINF/ CACHSIZ,CACHSZP1,CACHDIR(100),CACHPOS(100),
     &                  CACHFILE(100),CACHMOD(100),OLDEST
      DATA NAMES /'MOINTS  ','GAMLAM  ','MOABCD  ','DERINT  ',
     &            'DERGAM  '/
      LENMOIO=5000
      IF(INTIME.EQ.-1)THEN
       CALL IZERO(MOIO,LENMOIO)
       CALL IZERO(MOIODS,LENMOIO)
       CALL IZERO(MOIOSZ,LENMOIO)
       CALL IZERO(MOIOWD,LENMOIO)
       CALL IZERO(MOIOFL,LENMOIO)
      ELSE
       DO 10 I=50,54
        NSPLIT=NUMSPLIT(I-49)
        IF(NSPLIT.EQ.0)THEN
         IPOS=ISRCHEQ_C4(LENMOIO,MOIOFL,1,I)
         IF(IPOS.NE.LENMOIO+1)THEN
          CALL GFNAME(NAMES(I-49),FNAME,ILENGTH)
          INQUIRE(FILE=FNAME(1:ILENGTH),EXIST=YESNO,OPENED=ISOPN)
          IF(YESNO.AND..NOT.ISOPN) THEN
           CALL OPNFIL_C4(I-49)
          ELSE IF(.NOT.YESNO) THEN
           WRITE(6,1000)I
1000       FORMAT(T3,'@INITIO-F, Unit ',I3,' contained in list '
     &              ,'directory, but is not on disk.')
           CALL ERREX
          ENDIF
         ENDIF
        ELSE
         IPOS=ISRCHEQ_C4(LENMOIO,MOIOFL,1,I)
         IF(IPOS.NE.LENMOIO+1)THEN
          CALL GFNAME(NAMES(I-49),FNAME,ILENGTH)
          INQUIRE(FILE=FNAME(1:ILENGTH),EXIST=YESNO,OPENED=ISOPN)
          IF(YESNO.AND..NOT.ISOPN) THEN
           CALL OPNFIL_C4(I-49)
          ELSE IF(.NOT.YESNO) THEN
           WRITE(6,1000)I
           CALL ERREX
          ENDIF
         ENDIF
        ENDIF
10     CONTINUE
      ENDIF
      IF(INTIME.NE.0)CALL IZERO(CACHDIR,100)
      OLDEST=1
      RETURN
      END
