      SUBROUTINE GETSTF_C4(EVAL,IUHF,IENTRY)
      IMPLICIT DOUBLE PRECISION (A-H,O-Z)
      LOGICAL YESNO,LINCC,CICALC
      CHARACTER*3 CALTYP
      CHARACTER*80 FNAME
      INTEGER DIRPRD,QUIKGET
      DIMENSION EVAL(1)
      COMMON /LISTS/ MOIO(10,500),MOIOWD(10,500),MOIOSZ(10,500),
     &               MOIODS(10,500),MOIOFL(10,500)
      COMMON /MACHSP/ IINTLN,IFLTLN,IINTFP,IALONE,IBITWD
      COMMON /FILES/ LUOUT,MOINTS
      COMMON /FILSPC/ ILNBUF,IPRCLN,IPRCWD
      COMMON /INFO/ NOCCO(2),NVRTO(2)
      COMMON /FSTRIPE/ NUMSPLIT(5)
      COMMON /SYMINF/ NSTART,NIRREP,IRREPA(255),IRREPB(255),
     &DIRPRD(8,8)
      COMMON /SYMLOC/ ISYMOFF(8,8,25)
      COMMON /SYMPOP/ IRPDPD(8,22),ISYTYP(2,500),NTOT(18)
      COMMON /CONTROL/ IPRNT,IXXX,IXXX2
      COMMON /FLAGS/ IFLAGS(100)
      COMMON /FLAGS2/ IFLAGS2(100)
      COMMON /LINEAR/ LINCC,CICALC
      COMMON /WHERE/ QUIKGET(10,500)
      MOIOSIZ=5000
      IONE=1
      ITWO=2
      IFIVE=5
      ISIXTN=16
      ISIXFR=64
      NPARAM=100
      IUHF=0
      LINCC=.FALSE.
      CICALC=.FALSE.
      CALL IZERO(QUIKGET,5000)
      CALL GETREC_C4(-1,'JOBARC','IFLAGS  ',NPARAM,IFLAGS)
      CALL GETREC_C4(-1,'JOBARC','IFLAGS2 ',NPARAM,IFLAGS2)
      CALL GETREC_C4(-1,'JOBARC','NOCCORB ',ITWO,NOCCO)
      CALL GETREC_C4(-1,'JOBARC','NVRTORB ',ITWO,NVRTO)
      CALL GETREC_C4(-1,'JOBARC','COMPNIRR',IONE,NIRREP)
      CALL GETREC_C4(-1,'JOBARC','NUMSPLIT',IFIVE,NUMSPLIT)
      CALL GETDPT_C4
      IF(IFLAGS(11).GE.1)IUHF=1
      NORBS=NOCCO(1)+NVRTO(1)
      CALTYP='RHF'
      IF(IUHF.EQ.1)CALTYP='UHF'
      CALL FILPOP_C4
      CALL INITPOP_C4
      CALL GETREC_C4(-1,'JOBARC','MOIOVEC ',MOIOSIZ,MOIO)
      CALL GETREC_C4(-1,'JOBARC','MOIOWRD ',MOIOSIZ,MOIOWD)
      CALL GETREC_C4(-1,'JOBARC','MOIOSIZ ',MOIOSIZ,MOIOSZ)
      CALL GETREC_C4(-1,'JOBARC','MOIODIS ',MOIOSIZ,MOIODS)
      CALL GETREC_C4(-1,'JOBARC','MOIOFIL ',MOIOSIZ,MOIOFL)
      CALL GETREC_C4(-1,'JOBARC','ISYMTYP ',1000,ISYTYP)
      CALL UPDMOI_C4(0,0,0,0,2,0)
      CALL GFNAME('MOINTS  ',FNAME,ILENGTH)
      INQUIRE(FILE=FNAME(1:ILENGTH),EXIST=YESNO)
      IF(YESNO.AND.IENTRY.NE.-1)THEN
       CALL UPDMOI_C4(0,0,0,0,3,0)
       IJUNK=0
       CALL INITIO_C4(IJUNK,I0) 
      ENDIF
      CALL GNSYOF_C4
      RETURN
      END
