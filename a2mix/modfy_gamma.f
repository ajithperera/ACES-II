      SUBROUTINE MODFY_GAMMA(GAMMA, GAMMATMP, LENGTH, IMOMN, JMOMN,
     &                       KMOMN, LMOMN, INCRF, JNCRF, KNCRF, LNCRF,
     &                       IANGMOM, JANGMOM, KANGMOM, LANGMOM)
C
      IMPLICIT DOUBLE PRECISION(A-H, O-Z)
C
      DIMENSION GAMMATMP(LENGTH), GAMMA(LENGTH)
C  
      INDEX(I,J,K,L) = IANGMOM*(JANGMOM*(KANGMOM*(L-1)+K-1)+J-1)+I
C
      NTOTCRF = INCRF*JNCRF*KNCRF*LNCRF
C
      CALL ZERO(GAMMA, NTOTCRF)
C
      IOFF = NTOTCRF*(INDEX(IMOMN, JMOMN, KMOMN, LMOMN) - 1)
C
      DO NGAMMA = 1, NTOTCRF

         GAMMA(NGAMMA) = GAMMATMP(IOFF + NGAMMA)

      ENDDO
C
      RETURN
      END


