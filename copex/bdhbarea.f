      SUBROUTINE BDHBAREA(EEA,WBAR,FIJ,FIA,FAB,T2,F,NOCC,NVIR,NEA,NAO)
C
      IMPLICIT DOUBLE PRECISION (A-H, O-Z) 
C
      DIMENSION EEA(NEA,NEA)
      DIMENSION WBAR(NAO,NAO,NAO,NAO)
      DIMENSION FIJ(NOCC,NOCC)
      DIMENSION FIA(NOCC,NVIR)
      DIMENSION FAB(NVIR,NVIR)
      DIMENSION T2(NOCC,NOCC,NVIR,NVIR)
      DIMENSION F(NAO)
C
      DZERO = 0.D+0
C
      DO 5 I1=1, NEA
         DO 6 I2=1, NEA
            EEA(I1,I2)=DZERO
 6       CONTINUE
 5    CONTINUE
C
C HPP
      CALL DIAG1E(EEA,FAB,F,NVIR,NOCC,NEA,NAO)
C
C HQP
      CALL DIAG2E(EEA,WBAR,NVIR,NEA,NOCC,NAO)
C
C HPQ
      CALL DIAG3E(EEA,WBAR,NVIR,NEA,NOCC,NAO)
      CALL DIAG4E(EEA,FIA,NVIR,NEA,NOCC,NAO)
C
C HQQ
      CALL DIAG5E(EEA,WBAR,NVIR,NEA,NOCC,NAO)
      CALL DIAG6E(EEA,WBAR,NVIR,NEA,NOCC,NAO)
      CALL DIAG7E(EEA,WBAR,T2,NVIR,NEA,NOCC,NAO)
      CALL DIAG8E(EEA,FIJ,FAB,F,NVIR,NEA,NOCC,NAO)
C
      RETURN
      END
