      SUBROUTINE LISTINFO(IUHF, ITYP)
C
C  PRINTS OUT THE LISTINFO ON THE S-LISTS AND SOME OTHER LISTS
C
      IMPLICIT INTEGER (A-Z)
      LOGICAL LEFTHAND, EXCICORE, singonly,
     $   DROPCORE, printall
C
      COMMON /LISTS/ MOIO(10,500),MOIOWD(10,500),MOIOSZ(10,500),
     &   MOIODS(10,500),MOIOFL(10,500)
      COMMON/SLISTS/LS1IN, LS1OUT, LS2IN(2,2), LS2OUT(2,2)
      COMMON/LISTDAV/LISTC, LISTHC, LISTH0
      COMMON/LISTDIP/DIPOO(2), DIPOV(2), DIPVV(2)
      COMMON/LISTDENS/DENSOO(2), DENSOV(2), DENSVV(2)
      COMMON/LISTF/LISTFJA, LISTFAJ, LISTFAB, LISTFIJ,
     $   LISTF0, NDIMFLST(2,4)
      COMMON/LISTU/LISTUIJ, LISTUAB, LISTUAI
      COMMON/EACALC/LEFTHAND, EXCICORE, SINGONLY, DROPCORE
      COMMON /SYMINF/ NSTART,NIRREP,IRREPS(255,2),DIRPRD(8,8)
      COMMON/STINFO/ITOTALS, STMODE, LISTST, STCALC, NSIZEST
      printall =.true.
C
C  PRINT S1 LIST INFO
C
      if (printall) then
      WRITE(6,*) 'INFO ON S1IN LIST', LS1IN
      LIST = LS1IN
      DO 10 ISPIN = 1, 1+IUHF
         WRITE(6,*) MOIO(ISPIN, LIST), MOIOWD(ISPIN,LIST),
     $      MOIOSZ(ISPIN,LIST), MOIODS(ISPIN, LIST)
 10   CONTINUE
C
            WRITE(6,*) 'INFO ON S1OUT LIST', LS1OUT
      LIST = LS1OUT
      DO 20 ISPIN = 1, 1+IUHF
         WRITE(6,*) MOIO(ISPIN, LIST), MOIOWD(ISPIN,LIST),
     $     MOIOSZ(ISPIN,LIST), MOIODS(ISPIN, LIST)
 20   CONTINUE
C
      WRITE(6,*) ' INFO ON S2IN LISTS'
      DO 30 ISPIN = 1, 1+IUHF
         DO 40 MSPIN = 1, 1+IUHF
            LIST = LS2IN(ISPIN, MSPIN+1-IUHF)
            WRITE(6,*) ISPIN, MSPIN, LIST
            DO 50 XIRREP = 1, NIRREP
               WRITE(6,*) MOIO(XIRREP, LIST), MOIOWD(XIRREP,LIST),
     $            MOIOSZ(XIRREP,LIST), MOIODS(XIRREP, LIST)
 50         CONTINUE
 40      CONTINUE
 30   CONTINUE
C
      WRITE(6,*) ' INFO ON S2OUT LISTS'
      DO 60 ISPIN = 1, 1+IUHF
         DO 70 MSPIN = 1, 1+IUHF
            LIST = LS2OUT(ISPIN, MSPIN+1-IUHF)
            WRITE(6,*) ISPIN, MSPIN, LIST
            DO 80 XIRREP = 1, NIRREP
               WRITE(6,*) MOIO(XIRREP, LIST), MOIOWD(XIRREP,LIST),
     $            MOIOSZ(XIRREP,LIST), MOIODS(XIRREP, LIST)
 80         CONTINUE
 70      CONTINUE
 60   CONTINUE      
C
      endif
      IF ((ITYP.EQ.2) .OR. (ITYP.EQ.3)) THEN
C
      WRITE(6,*) ' INFO ON LISTS USED IN DAVIDSON SCHEME'
      IF (LEFTHAND) THEN
         NUMLST = 2
      ELSE
         NUMLST = 1
      ENDIF
C
      WRITE(6, *) ' LISTC = ', LISTC
      LIST = LISTC
      DO 90 I = 1, NUMLST
         WRITE(6,*) MOIO(I, LIST), MOIOWD(I,LIST),
     $      MOIOSZ(I,LIST), MOIODS(I,LIST)
 90   CONTINUE
C
      WRITE(6, *) ' LISTHC = ', LISTHC
      LIST = LISTHC
      DO 100 I = 1, NUMLST
         WRITE(6,*) MOIO(I, LIST), MOIOWD(I,LIST),
     $      MOIOSZ(I,LIST), MOIODS(I,LIST)
 100   CONTINUE
C
      WRITE(6, *) ' LISTH0 = ', LISTH0
      LIST = LISTH0
      I=1
      WRITE(6,*) MOIO(I, LIST), MOIOWD(I,LIST),
     $   MOIOSZ(I,LIST), MOIODS(I,LIST)
C
      WRITE(6, *) ' LISTST = ', LISTST
      LIST = LISTST
      I=1
      WRITE(6,*) MOIO(I, LIST), MOIOWD(I,LIST),
     $   MOIOSZ(I,LIST), MOIODS(I,LIST)
C
      ENDIF
C
      IF (EXCICORE) THEN
         WRITE(6,*) ' INFO ON DIPOLE LISTS '
C
      WRITE(6, *) ' DIPOO(ALPHA) = ', DIPOO(1)
      LIST = DIPOO(1)
      DO 101 I = 1, NIRREP
         WRITE(6,*) MOIO(I, LIST), MOIOWD(I,LIST),
     $      MOIOSZ(I,LIST), MOIODS(I,LIST)
 101  CONTINUE
C
      WRITE(6, *) ' DIPOO(BETA)  = ', DIPOO(2)
      LIST = DIPOO(2)
      DO 102 I = 1, NIRREP
         WRITE(6,*) MOIO(I, LIST), MOIOWD(I,LIST),
     $      MOIOSZ(I,LIST), MOIODS(I,LIST)
 102  CONTINUE
C
      WRITE(6, *) ' DIPOV(ALPHA) = ', DIPOV(1)
      LIST = DIPOV(1)
      DO 103 I = 1, NIRREP
         WRITE(6,*) MOIO(I, LIST), MOIOWD(I,LIST),
     $      MOIOSZ(I,LIST), MOIODS(I,LIST)
 103  CONTINUE
C
      WRITE(6, *) ' DIPOV(BETA)  = ', DIPOV(2)
      LIST = DIPOV(2)
      DO 104 I = 1, NIRREP
         WRITE(6,*) MOIO(I, LIST), MOIOWD(I,LIST),
     $      MOIOSZ(I,LIST), MOIODS(I,LIST)
 104  CONTINUE
C
      WRITE(6, *) ' DIPVV(ALPHA) = ', DIPVV(1)
      LIST = DIPVV(1)
      DO 105 I = 1, NIRREP
         WRITE(6,*) MOIO(I, LIST), MOIOWD(I,LIST),
     $      MOIOSZ(I,LIST), MOIODS(I,LIST)
 105  CONTINUE
C
      WRITE(6, *) ' DIPVV(BETA) = ', DIPVV(2)
      LIST = DIPVV(2)
      DO 106 I = 1, NIRREP
         WRITE(6,*) MOIO(I, LIST), MOIOWD(I,LIST),
     $      MOIOSZ(I,LIST), MOIODS(I,LIST)
 106  CONTINUE
C
         WRITE(6,*) ' INFO ON DENSITY LISTS '
C
      WRITE(6, *) ' DENSOO(ALPHA) = ',  DENSOO(1)
      LIST =  DENSOO(1)
      DO 201 I = 1, NIRREP
         WRITE(6,*) MOIO(I, LIST), MOIOWD(I,LIST),
     $      MOIOSZ(I,LIST), MOIODS(I,LIST)
 201  CONTINUE
C
      WRITE(6, *) '  DENSOO(BETA)  = ', DENSOO(2)
      LIST = DENSOO(2)
      DO 202 I = 1, NIRREP
         WRITE(6,*) MOIO(I, LIST), MOIOWD(I,LIST),
     $      MOIOSZ(I,LIST), MOIODS(I,LIST)
 202  CONTINUE
C
      WRITE(6, *) ' DENSOV(ALPHA) = ', DENSOV(1)
      LIST = DENSOV(1)
      DO 203 I = 1, NIRREP
         WRITE(6,*) MOIO(I, LIST), MOIOWD(I,LIST),
     $      MOIOSZ(I,LIST), MOIODS(I,LIST)
 203  CONTINUE
C
      WRITE(6, *) ' DENSOV(BETA)  = ', DENSOV(2)
      LIST = DENSOV(2)
      DO 204 I = 1, NIRREP
         WRITE(6,*) MOIO(I, LIST), MOIOWD(I,LIST),
     $      MOIOSZ(I,LIST), MOIODS(I,LIST)
 204  CONTINUE
C
      WRITE(6, *) ' DENSVV(ALPHA) = ', DENSVV(1)
      LIST = DENSVV(1)
      DO 205 I = 1, NIRREP
         WRITE(6,*) MOIO(I, LIST), MOIOWD(I,LIST),
     $      MOIOSZ(I,LIST), MOIODS(I,LIST)
 205  CONTINUE
C
      WRITE(6, *) ' DENSVV(BETA) = ', DENSVV(2)
      LIST = DENSVV(2)
      DO 206 I = 1, NIRREP
         WRITE(6,*) MOIO(I, LIST), MOIOWD(I,LIST),
     $      MOIOSZ(I,LIST), MOIODS(I,LIST)
 206  CONTINUE
C
      ENDIF
C
      IF (EXCICORE) THEN
         WRITE(6,*) 'WRITE INFO ON LISTS FJA ', LISTFJA
         LIST = LISTFJA
         DO 410 I = 1, 2
            WRITE(6,*) MOIO(I, LIST), MOIOWD(I,LIST),
     $         MOIOSZ(I,LIST), MOIODS(I,LIST)
 410     CONTINUE
C
         WRITE(6,*) 'WRITE INFO ON LISTS FAJ ', LISTFAJ
         LIST = LISTFAJ
         DO 420 I = 1, 2
            WRITE(6,*) MOIO(I, LIST), MOIOWD(I,LIST),
     $         MOIOSZ(I,LIST), MOIODS(I,LIST)
 420     CONTINUE
C
         WRITE(6,*) 'WRITE INFO ON LISTS FAB ', LISTFAB
         LIST = LISTFAB
         DO 430 I = 1, 2
            WRITE(6,*) MOIO(I, LIST), MOIOWD(I,LIST),
     $         MOIOSZ(I,LIST), MOIODS(I,LIST)
 430     CONTINUE
C
         WRITE(6,*) 'WRITE INFO ON LISTS FIJ ', LISTFIJ
         LIST = LISTFIJ
         DO 435 I = 1, 2
            WRITE(6,*) MOIO(I, LIST), MOIOWD(I,LIST),
     $         MOIOSZ(I,LIST), MOIODS(I,LIST)
 435     CONTINUE
C
         WRITE(6,*) 'WRITE INFO ON LISTS UIJ ', LISTUIJ
         LIST = LISTUIJ
         DO 440 I = 1, 2
            WRITE(6,*) MOIO(I, LIST), MOIOWD(I,LIST),
     $         MOIOSZ(I,LIST), MOIODS(I,LIST)
 440     CONTINUE
C
         WRITE(6,*) 'WRITE INFO ON LISTS UAB ', LISTUAB
         LIST = LISTUAB
         DO 450 I = 1, 2
            WRITE(6,*) MOIO(I, LIST), MOIOWD(I,LIST),
     $         MOIOSZ(I,LIST), MOIODS(I,LIST)
 450     CONTINUE
C
         WRITE(6,*) 'WRITE INFO ON LISTS UAI ', LISTUAI
         LIST = LISTUAI
         DO 460 I = 1, 2
            WRITE(6,*) MOIO(I, LIST), MOIOWD(I,LIST),
     $         MOIOSZ(I,LIST), MOIODS(I,LIST)
 460     CONTINUE
C
      ENDIF
C
      RETURN
      END
