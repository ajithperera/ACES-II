      SUBROUTINE PRINTT1(T1, IPOP1, IPOP2)
C
C A COEFFICIENT ARRAY ORGANIZED LIKE T1 IS PRINTED OUT.
C IPOP1 : POPULATION INDEX FOR ROW OF T1
C IPOP2 : POPULATION INDEX FOR COLUMNS OF T1
C
      IMPLICIT DOUBLE PRECISION (A-H,O-Z)
      DIMENSION T1(*), IPOP1(8), IPOP2(8)
C
      COMMON /SYMINF/ NSTART,NIRREP,IRREPA(255),IRREPB(255),DIRPRD(8,8)
C
      ICOUNT = 1
      DO 10 IRREP = 1, NIRREP
         NROW = IPOP1(IRREP)
         NCOL = IPOP2(IRREP)
         IF (NROW * NCOL .GT. 0) THEN
            WRITE(6,*)' IRREP ', IRREP
            CALL OUTPUT(T1(ICOUNT),1,NROW, 1,NCOL, NROW, NCOL,1)
            ICOUNT = ICOUNT + NROW*NCOL
         ENDIF
 10   CONTINUE
C
      RETURN
      END
