      LOGICAL FUNCTION ORDERED(ASYMCASE, A, B)
C
C  DETERMINES IF A COEFFICIENT IS TO BE PRINTED OR NOT
C
      IMPLICIT INTEGER (A-Z)
C
      ORDERED = .FALSE.
      IF (ASYMCASE .EQ. 1) THEN
         ORDERED = .TRUE.
      ELSEIF ((ASYMCASE .EQ. 2) .AND. (A .LT. B)) THEN
         ORDERED = .TRUE.
      ENDIF
C
      RETURN
      END
