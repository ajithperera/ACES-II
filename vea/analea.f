      SUBROUTINE ANALEA(IUHF, IRREP, ISPIN)
C
C  THE CURRENT BLOCK OF ELECTRON AFFINITIES IS SUMMARIZED
C  THE ELECTRO-AFFINITIES AND OSCILLATOR STRENGHTS ARE PRINTED.
C
      IMPLICIT DOUBLE PRECISION (A-H, O-Z)
      CHARACTER *5 SPIN(2)
      CHARACTER *7 MULTPLCT(2)
      INTEGER SPINCORE, STMODE, STCALC
      LOGICAL LEFTHAND, EXCICORE, SINGONLY, DROPCORE
C
      COMMON/ROOTS/EIGVAL(100,8,3), OSCSTR(100,8,3)
      COMMON /SYMINF/ NSTART,NIRREP,IRREPS(255,2),DIRPRD(8,8)
      COMMON/EAINFO/NUMROOT(8,3)
      COMMON/MACHSP/IINTLN,IFLTLN,IINTFP,IALONE,IBITWD
      COMMON/EACALC/LEFTHAND, EXCICORE, SINGONLY, DROPCORE
      COMMON/COREINFO/IREPCORE, SPINCORE, IORBCORE, IORBOCC
      COMMON/IPCORE/COREIP
      COMMON/STINFO/ITOTALS, STMODE, LISTST, STCALC, NSIZEST
C
      DATA MULTPLCT /'SINGLET', 'TRIPLET'/
      DATA SPIN /'ALPHA', 'BETA '/
      DATA FACTEV, FACTCM /27.2113957D0,  2.19474625D5/
C
      IF (EXCICORE) THEN
C
         IS = ITOTALS
         WRITE(6,1300)
 1300    FORMAT(/)
         WRITE(6,*)'             CURRENT BLOCK OF ELECTRO-AFFINITIES'
         IF ((ISPIN .NE. SPINCORE) .OR. (STMODE .EQ. 2)) THEN
            WRITE(6,1020) SPIN(ISPIN),MULTPLCT(IS+1), IRREP
 1020       FORMAT(3X, A5, ' SPIN, MULTIPLICITY : ',
     $         A7,'  SYMMETRY BLOCK: ', I2)
         ELSE
            WRITE(6,1020) SPIN(ISPIN), 'UNKNOWN',IRREP
         ENDIF
         WRITE(6,1300)
         WRITE(6, 1015)
 1015    FORMAT(4X, 'ROOT #',9X, 'EA(eV)',13X, 'EE(eV)',13x,
     $      'OSC. STRENGTH')
         WRITE(6,*)'     --------------------------------------'
         DO 130 IROOT = 1, NUMROOT(IRREP, ISPIN+1-IS)
            EA = EIGVAL(IROOT, IRREP, ISPIN+1-IS) * FACTEV
            EE = EA + COREIP * FACTEV
            OS = OSCSTR(IROOT, IRREP, ISPIN+1-IS)
            WRITE(6,1030) IROOT, EA, EE, OS
 130     CONTINUE
C
 1030    FORMAT(3X, I4, 5X, F15.8, 5X, F15.8, 5X, E16.5)
C
      ELSE
C
         IS = ITOTALS
         WRITE(6,1300)
         WRITE(6,*)'            CURRENT BLOCK OF ELECTRO-AFFINITIES'
         WRITE(6,2020) SPIN(ISPIN), IRREP
 2020       FORMAT(3X, A5, ' SPIN : ',
     $         '  SYMMETRY BLOCK: ', I2)
         WRITE(6,1300)
         WRITE(6, 2015)
 2015    FORMAT(4X, 'ROOT #',9X, 'EA(eV)',13X, 'EA(A.U.)')
         WRITE(6,*)'     --------------------------------------'
         DO 230 IROOT = 1, NUMROOT(IRREP, ISPIN+1-IS)
            EA = EIGVAL(IROOT, IRREP, ISPIN+1-IS) * FACTEV
            EE = EIGVAL(IROOT, IRREP, ISPIN+1-IS)
            WRITE(6,2030) IROOT, EA, EE
 230     CONTINUE
C
 2030    FORMAT(3X, I4, 5X, F15.8, 5X, F15.8)
C
      ENDIF
C
      RETURN
      END
