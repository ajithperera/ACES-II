      SUBROUTINE SEMI2STAN(T1,SCR,ISPIN)
      IMPLICIT DOUBLE PRECISION (A-H,O-Z)
      DIMENSION T1(*),SCR(*)
      INTEGER POP,VRT,DIRPRD
      COMMON /INFO/ NOCCO(2),NVRTO(2)
      COMMON /MACHSP/ IINTLN,IFLTLN,IINTFP,IALONE,IBITWD
      COMMON /SYM/ POP(8,2),VRT(8,2),NT(2),NF(4)
      COMMON /SYMLOC/ ISYMOFF(8,8,25)
      COMMON /FLAGS/ IFLAGS(100)
      COMMON /SYMINF/NSTART,NIRREP,IRREPS(255,2),DIRPRD(8,8)
      DATA ONE,ONEM,ZILCH/1.0D0,-1.0D0,0.0D0/
      CALL GNSYOF
      NOSIZ=ISYMOFF(NIRREP,1,20+ISPIN)+
     &POP(NIRREP,ISPIN)*POP(NIRREP,ISPIN)-1
      NVSIZ=ISYMOFF(NIRREP,1,18+ISPIN)+
     &VRT(NIRREP,ISPIN)*VRT(NIRREP,ISPIN)-1
      I000=1
      I010=I000+NOSIZ*NOSIZ
      I020=I010+NVSIZ*NVSIZ
      I030=I020+NT(ISPIN)
      IF(ISPIN.EQ.1)THEN
       CALL GETREC(20,'JOBARC','WMATRXOA',IINTFP*NOSIZ,SCR(I000))
       CALL GETREC(20,'JOBARC','WMATRXVA',IINTFP*NVSIZ,SCR(I010))
      ELSE
       CALL GETREC(20,'JOBARC','WMATRXOB',IINTFP*NOSIZ,SCR(I000))
       CALL GETREC(20,'JOBARC','WMATRXVB',IINTFP*NVSIZ,SCR(I010))
      ENDIF
      IOFFO=I000
      IOFFV=I010
      IOFFTS=I020
      IOFFTC=1
      IOFFS=I030
      DO 10 IRREP=1,NIRREP
       NOCC=POP(IRREP,ISPIN)
       NVRT=VRT(IRREP,ISPIN)
       CALL XGEMM('N','T',NVRT,NOCC,NOCC,ONE,T1(IOFFTC),NVRT,
     &            SCR(IOFFO),NOCC,ZILCH,SCR(IOFFS),NVRT)
       CALL XGEMM('N','N',NVRT,NOCC,NVRT,ONE,SCR(IOFFV),NVRT,
     &            SCR(IOFFS),NVRT,ZILCH,SCR(IOFFTS),NVRT)
       IOFFTC=IOFFTC+NVRT*NOCC
       IOFFTS=IOFFTS+NVRT*NOCC
       IOFFV =IOFFV+NVRT*NVRT
       IOFFO =IOFFO+NOCC*NOCC
10    CONTINUE
      CALL SCOPY(NT(ISPIN),SCR(I020),1,T1,1)
      ipos=isamax(nt(ispin),t1,1)
      write(6,*)' largest standard t1 element ',t1(ipos)
      RETURN
      END
