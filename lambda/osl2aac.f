      SUBROUTINE OSL2AAC(L,T,G,T1A,T1B,ISPIN,POPL,POPT1,POPT2,VRT1,VRT2,
     $   NOCCSQ,DISSYW,DISSYT,NUMSYW,NUMSYT,LISTL,IRREP,FACT)
C
      IMPLICIT DOUBLE PRECISION (A-H,O-Z)
      DOUBLE PRECISION L 
      INTEGER DISSYT,DISSYW,DIRPRD,POPL,POPT1,POPT2,VRT1,VRT2
      DIMENSION L(DISSYW,NUMSYW),T(DISSYT,NUMSYT),G(NOCCSQ)
      DIMENSION POPL(8),POPT1(8),POPT2(8),VRT1(8),VRT2(8)
      COMMON/MACHSP/IINTLN,IFLTLN,IINTFP,IALONE,IBITWD
      COMMON /SYMINF/ NSTART,NIRREP,IRREPA(255),IRREPB(255),DIRPRD(8,8)
C
      DATA ONEM,ONE,HALF,HALFM /-1.0D0,1.D0,0.5D0,-0.5D0/
C
      CALL FSGET(L,1,NUMSYW,1,IRREP,LISTL,'NINI')
C
      CALL ZERO(T,NUMSYT*DISSYT)
      CALL FTAU(T,T1A,T1B,DISSYT,NUMSYT,POPT1,POPT2,VRT1,VRT2,IRREP,
     $     3,ONE)
C
      JOFFL=1
      JOFFT=1
      IOFF=1
      DO 90 IRREPI=1,NIRREP
C
         NOCCL=POPL(IRREPI)
         NOCCT=POPT2(IRREPI)
C
         IRREPJ=DIRPRD(IRREP,IRREPI)
         NOCCJ=POPT1(IRREPJ)
C
         IF(MIN(NOCCT,NOCCL,NOCCJ).NE.0) THEN
C
            CALL XGEMM('T','N',NOCCT,NOCCL,DISSYW*NOCCJ,FACT*ONEM,
     &         T(1,JOFFT),NOCCJ*DISSYW,L(1,JOFFL),
     &         NOCCJ*DISSYW,ONE,G(IOFF),NOCCT)
         ENDIF
C
         JOFFT=JOFFT+NOCCT*NOCCJ
         JOFFL=JOFFL+NOCCL*NOCCJ
         IOFF=IOFF+NOCCL*NOCCT
 90   CONTINUE
C
      RETURN
      END
