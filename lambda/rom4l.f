      SUBROUTINE ROM4L(ICORE,MAXCOR,IUHF)
C
C CALCULATES THE THIRD-ORDER LAMBDA AMPLITUDES IN FOURTH-ORDER
C ROHF-MBPT
C
CEND
      IMPLICIT DOUBLE PRECISION(A-H,O-Z)
      INTEGER DIRPRD,POP,VRT,DISSYL
C
      DIMENSION ICORE(MAXCOR)
C
      COMMON/SYMINF/NSTART,NIRREP,IRREPA(255,2),DIRPRD(8,8)
      COMMON/INFO/NOCCO(2),NVRTO(2)
      COMMON/SYM/POP(8,2),VRT(8,2),NT(2),NF1(2),NF2(2)
      COMMON/MACHSP/IINTLN,IFLTLN,IINTFP,IALONE,IBITWD
      COMMON/SYMPOP/IRPDPD(8,22),ISYTYP(2,500),NTOT(18)
C
      DATA ONE/1.0D0/
C
C FORM L1[3] AMPLITUDES
C
C   ALLOCATE MEMORY FOR L1[3] AMPLITUDES
C
      I000=1
      I010=I000+IINTFP*NT(1)
      I020=I010+IINTFP*NT(2)
      MXCOR=MAXCOR-IINTFP*(NT(1)+NT(2))
      CALL IZERO(ICORE(I000),IINTFP*(NT(1)+NT(2)))
C
C GENERATE SECOND-ORDER INTERMEDIATES
C FIRST F- AND W-INTERMEDIATES AS NEEDED FOR THE ENERGY CALCULATION
C GENERATE IN ADDITION W(TWIDDLE)
C
C  CONTRACT WITH FIRST-ORDER LAMBDA-AMPLITUDES
C
C
C GENERATE FIRST-ORDER INTERMEDIATES
C
C
C CONTRACT WITH SECOND-ORDER LAMBDA-AMPLITUDES
C
      RETURN
      END
