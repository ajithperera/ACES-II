      SUBROUTINE OUTPRD(V1,V2,PROD,NSIZV1,NSIZV2,FACT)
C
C THIS ROUTINE COMPUTES THE OUTER PRODUCT OF TWO VECTORS.
C
C INPUT: 
C        V1 - A VECTOR OF LENGTH NSIZV1.
C        V2 - A VECTOR OF LENGTH NSIZV2.
C        
C OUTPUT:
C      PROD - THE OUTER PRODUCT OF V1 AND V2 (A MATRIX OF DIMENSION
C                NSIZV1 x NSIZV2.
C
CEND
      IMPLICIT DOUBLE PRECISION (A-H,O-Z)
      DIMENSION V1(NSIZV1,1),V2(1,NSIZV2),PROD(NSIZV1,NSIZV2)
      DATA ZILCH /0.0/
      CALL XGEMM('N','N',NSIZV1,NSIZV2,1,-FACT,V1,NSIZV1,
     &           V2,1,ZILCH,PROD,NSIZV1) 
      RETURN
      END
