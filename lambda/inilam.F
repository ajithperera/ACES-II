      SUBROUTINE INILAM(IUHF)
C
C     THIS ROUTINE INITIALIZES THE LISTS
C     FOR THE LAMBDA CONTRIBUTIONS 
C
CEND
C
C  CODED AUGUST/90
C
      IMPLICIT INTEGER(A-Z)
      LOGICAL MBPT2,MBPT3,M4DQ,M4SDQ,M4SDTQ,CCD,QCISD,CCSD,UCC,CC2
      LOGICAL NO_REDUNDANT
      COMMON/METH/MBPT2,MBPT3,M4DQ,M4SDQ,M4SDTQ,CCD,QCISD,CCSD,UCC,CC2
      COMMON/FLAGS/IFLAGS(100)
      COMMON /FLAGS2/ IFLAGS2(500)
C
      IOFFW=0
      IF(IFLAGS(3).EQ.2) THEN
       IOFFW=100
      ENDIF
C
      IMODE=0
      NO_REDUNDANT = iflags2(155).ne.0
C
C RESORTED L2 AMPLITUDES
C
      if(NO_REDUNDANT) then

         CALL INIT_NOREDNT_LIST(1,9,9,134)
         CALL INIT_NOREDNT_LIST(1,9,10,137)
         CALL INIT_NOREDNT_LIST(1,11,12,139)
         IF(IUHF.NE.0) THEN
            CALL INIT_NOREDNT_LIST(1,10,9,136)
            CALL INIT_NOREDNT_LIST(1,12,11,138)
            CALL INIT_NOREDNT_LIST(1,10,10,135)
         ENDIF
c  various tau distributions
         CALL INIPCK(1,9,9,234,IMODE,0,1)
         CALL INIPCK(1,9,10,237,IMODE,0,1)
         CALL INIPCK(1,11,12,239,IMODE,0,1)
         IF (IUHF.NE.0)THEN
            CALL INIPCK(1,10,9,236,IMODE,0,1)
            CALL INIPCK(1,12,11,238,IMODE,0,1)
            CALL INIPCK(1,10,10,235,IMODE,0,1)
         ENDIF
c
      else

         CALL INIPCK(1,9,9,134,IMODE,0,1)
         CALL INIPCK(1,9,10,137,IMODE,0,1)
         CALL INIPCK(1,11,12,139,IMODE,0,1)
         IF(IUHF.NE.0) THEN
            CALL INIPCK(1,10,9,136,IMODE,0,1)
            CALL INIPCK(1,12,11,138,IMODE,0,1)
            CALL INIPCK(1,10,10,135,IMODE,0,1)
         ENDIF

c endif no_redundant
      endif
      IF(CCSD .OR. CCD .OR.CC2)THEN
C
       CALL INIPCK(1,14,18,110+IOFFW,IMODE,0,1)
       IF(IUHF.NE.0)THEN
        CALL INIPCK(1,3,16,107+IOFFW,IMODE,0,1)
        CALL INIPCK(1,4,17,108+IOFFW,IMODE,0,1)
        CALL INIPCK(1,14,11,109+IOFFW,IMODE,0,1)
       ENDIF
C       
       IF(IUHF.NE.0)CALL INIPCK(1,10,9,117,IMODE,0,1)
       CALL INIPCK(1,9,10,118,IMODE,0,1)
       CALL INIPCK(1,9,9,123,IMODE,0,1)
       IF(IUHF.NE.0)CALL INIPCK(1,10,10,124,IMODE,0,1)
       CALL INIPCK(1,11,11,125,IMODE,0,1)
       IF(IUHF.NE.0)CALL INIPCK(1,12,12,126,IMODE,0,1)
C                                      =
C FOR CCSD SECOND DERIVATIVES, WE SAVE W ON LIST 174-179 FOR LATER USE
C
       IF(IFLAGS(3).EQ.2)THEN
        CALL INIPCK(1,9,9,74,IMODE,0,1)
        CALL INIPCK(1,9,10,76,IMODE,0,1)
        CALL INIPCK(1,11,11,78,IMODE,0,1)
        IF(IUHF.NE.0)THEN
         CALL INIPCK(1,10,10,75,IMODE,0,1)
         CALL INIPCK(1,10,9,77,IMODE,0,1)
         CALL INIPCK(1,12,12,79,IMODE,0,1)
        ENDIF
       ENDIF
C
       CALL INIPCK(1,13,11,130+IOFFW,IMODE,0,1)
       IF(IUHF.NE.0)THEN
        CALL INIPCK(1,1,9,127+IOFFW,IMODE,0,1)
        CALL INIPCK(1,2,10,128+IOFFW,IMODE,0,1)
        CALL INIPCK(1,13,18,129+IOFFW,IMODE,0,1)
       ENDIF
C    
      ENDIF
C
C  ALL DONE
C
      RETURN
      END
