      SUBROUTINE OSL1(ICORE,MAXCOR,IUHF)
C
C     THIS ROUTINE DRIVES THE CALCULATION OF THE CONTRIBUTIONS TYPE2
C     (I.E. DERIVATIVE OF THE RENORMALIZATION TERM WITH DERIVATIVE OF
C     H(EFF)). FIRST THE QUANTITY LAMBDA*RENORM/H(EFF) IS CALCULATED.
C     THEN THIS NUMBER IS USED TO MULTPLY THE H(EFF) DERIVATIVE LISTS
C     TO GET THE LAMBDA CONTRIBUTIONS.
C     IN ADDITION H(EFF) DERIVATIVE IS ALSO ADDED TO THE LAMBDA LIST
C     AS THE CONTRIBUTION TYPE 1.
C
CEND
C
CPROGRAMED PS JUL 93
C
      IMPLICIT DOUBLE PRECISION (A-H,O-Z)
      INTEGER DIRPRD,DISSYT
C
      DIMENSION ICORE(MAXCOR)
C
      COMMON /MACHSP/ IINTLN,IFLTLN,IINTFP,IALONE,IBITWD
C
      INTEGER POP,VRT
      COMMON/SYM/ POP(8,2),VRT(8,2),NT(2),NF1(2),NF2(2)
C
      COMMON /SYMINF/ NSTART,NIRREP,IRREPA(255),IRREPB(255),
     &                DIRPRD(8,8)
      COMMON /SYMPOP/ IRPDPD(8,22),ISYTYP(2,500),NTOT(18)
C
      INTEGER FSDPDAN,FSDPDNA,FSDPDAA,FSDPDIN,FSDPDNI,FSDPDII,FSDPDAI,
     $   FSDPDIA
      COMMON /FSSYMPOP/ FSDPDAN(8,22),FSDPDNA(8,22),FSDPDAA(8,22),
     &                  FSDPDIN(8,22),FSDPDNI(8,22),FSDPDII(8,22),
     &                  FSDPDAI(8,22),FSDPDIA(8,22)
C
      COMMON /ACTIRR/ IRREPOA,IRREPOB,IRREPVA,IRREPVB
C
      COMMON /FLAGS/ IFLAGS(100)
      DATA ONE,ONEM,ZILCH/1.D0,-1.D0,0.D0/
C
C     CALCULATE WL WHICH IS THE QUANTITY LAMBDA*RENORM/H(EFF)
C
      WL=ZILCH
C     
C     T1 AND DISCONNECTED T2 CONTRIBUTIONS
C
      CALL OST1IA(ICORE,MAXCOR,IUHF,ONE,.TRUE.,WL)
      CALL OST1AI(ICORE,MAXCOR,IUHF,ONE,.TRUE.,WL)
      CALL OST1II(ICORE,MAXCOR,IUHF,ONE,.TRUE.,WL)
C
C     T2 CONTRIBUTIONS
C
      CALL OST2AAIA(ICORE,MAXCOR,IUHF,ONE,.TRUE.,WL)
      CALL OST2AIAA(ICORE,MAXCOR,IUHF,ONE,.TRUE.,WL)
      CALL OST2IIAA(ICORE,MAXCOR,IUHF,ONE,.TRUE.,WL)
      CALL OST2AAII(ICORE,MAXCOR,IUHF,ONE,.TRUE.,WL)
      CALL OST2AIAI1(ICORE,MAXCOR,IUHF,ONE,.TRUE.,WL)
      CALL OST2AIAI2(ICORE,MAXCOR,IUHF,ONE,.TRUE.,WL)
C
      CALL OST2IIAI1(ICORE,MAXCOR,IUHF,ONE,.TRUE.,WL)
      CALL OST2IIAI2(ICORE,MAXCOR,IUHF,ONE,.TRUE.,WL)
C
      CALL OST2AIII1(ICORE,MAXCOR,IUHF,ONE,.TRUE.,WL)
      CALL OST2AIII2(ICORE,MAXCOR,IUHF,ONE,.TRUE.,WL)
C
      CALL OST2IIII1(ICORE,MAXCOR,IUHF,ONE,.TRUE.,WL)
      CALL OST2IIII2(ICORE,MAXCOR,IUHF,ONE,.TRUE.,WL)
      CALL OST2IIII3(ICORE,MAXCOR,IUHF,ONE,.TRUE.,WL)
      CALL OST2IIII4(ICORE,MAXCOR,IUHF,ONE,.TRUE.,WL)
C
C     NOW MODIFY L2(Nm,Mn)=L2(Nm,Mn) + WL + 1
C     TO ACCOUNT FOR THE CONTRIBUTIONS TYPE2 AND TYPE1
C
      LISTL2=146
      IRREP=DIRPRD(IRREPOA,IRREPOB)
      NUMSYL=FSDPDAA(IRREP,ISYTYP(2,LISTL2))
      CALL FSGET(W,1,NUMSYL,1,IRREP,LISTL2,'AAAA')
      W=W+WL+ONE
      CALL FSPUT(W,1,NUMSYL,1,IRREP,LISTL2,'AAAA')
      CALL FSPUT(W,1,NUMSYL,1,IRREP,LISTL2,'AAAA')
      CALL FSPUT(W,1,NUMSYL,1,IRREP,136,'AAAA')
      CALL FSPUT(W,1,NUMSYL,1,IRREP,137,'AAAA')
      CALL FSPUT(W,1,NUMSYL,1,1,138,'AAAA')
      CALL FSPUT(W,1,NUMSYL,1,1,139,'AAAA')
C
      RETURN
      END
