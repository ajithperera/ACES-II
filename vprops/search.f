      SUBROUTINE SEARCH(IUNIT,A)
      IMPLICIT DOUBLE PRECISION (A-H,O-Z)
      CHARACTER*8 A,A1,B(4)
      DATA A1/'********'/
1     READ (IUNIT,END=3,ERR=3) B(1)
      IF(B(1).NE.A1)GOTO 1
c      BACKSPACE(IUNIT)
      READ(IUNIT,END=3,ERR=3)B
2     IF(B(4).NE.A) GO TO 1
      RETURN
3     WRITE (6,4) A,IUNIT
      ENDFILE 6
4     FORMAT(5X,A8,'NOT FOUND ON UNIT',I3)
      STOP
      END
