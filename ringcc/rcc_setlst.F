
      SUBROUTINE RCC_SETLST(WORK,MAXCOR,IUHF)
      IMPLICIT INTEGER (A-Z)
      DIMENSION WORK(MAXCOR)
      COMMON /SYMINF/ NSTART,NIRREP,IRREPA(255),IRREPB(255),
     &                DIRPRD(8,8)
      COMMON/SYM/POP(8,2),VRT(8,2),NTAA,NTBB,NF1AA,NF1BB,NF2AA,
     &           NF2BB
      COMMON /SYMPOP/ IRPDPD(8,22),ISYTYP(2,500),NTOT(18)
C
C DRIVER FOR POINTER CREATION FOR LISTS 51-59 (RING INTERMEDIATES)
C
      IMODE=0
      IF(IUHF.NE.0)CALL INIPCK(1,3,3,51,IMODE,0,1)
      IF(IUHF.NE.0)CALL INIPCK(1,4,4,52,IMODE,0,1)
      CALL INIPCK(1,14,14,53,IMODE,0,1)
      CALL INIPCK(1,9,9,54,IMODE,0,1)
      IF(IUHF.NE.0)CALL INIPCK(1,10,10,55,IMODE,0,1)
      CALL INIPCK(1,9,10,56,IMODE,0,1)
      IF(IUHF.NE.0)CALL INIPCK(1,10,9,57,IMODE,0,1)
      CALL INIPCK(1,11,11,58,IMODE,0,1)
      IF(IUHF.NE.0)CALL INIPCK(1,12,12,59,IMODE,0,1)

      DO 10 ISPIN=1,1+IUHF
       CALL INIPCK(1,ISYTYP(1,43+ISPIN),ISYTYP(2,43+ISPIN),
     &             60+ISPIN,IMODE,0,1)
       CALL ZERSYM(ICORE,60+ISPIN)
10    CONTINUE

      CALL INIPCK(1,ISYTYP(1,46),ISYTYP(2,46),63,IMODE,0,1)
      CALL ZERSYM(ICORE,63)
C
C CREATE RING INCREMENT AREA
C
      CALL INIPCK(1,9,10,42,IMODE,0,1)
      CALL INIPCK(1,11,12,43,IMODE,0,1)
      CALL INIPCK(1,10,10,41,IMODE,0,1)
      IF(IUHF.NE.0)THEN
       CALL INIPCK(1,9,9,40,IMODE,0,1)
      ENDIF

C These two lists keeps singlet and triplet spin-adapted <Ab|Ij>
C integrals. They get formed in rcc_abij_4rhf.F (RHF only).
      IF (IUHF .EQ. 0) THEN
         CALL INIPCK(1,19,21,197,IMODE,0,1)
         CALL INIPCK(1,19,21,198,IMODE,0,1)
      ENDIF 

      RETURN
      END
