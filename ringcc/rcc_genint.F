      SUBROUTINE RCC_GENINT(WORK,MAXCOR,IUHF)

      IMPLICIT DOUBLE PRECISION(A-H,O-Z)
      DIMENSION WORk(MAXCOR)

      LOGICAL MBPT2,MBPT3,M4DQ,M4SDQ,M4SDTQ,CCD,QCISD,CCSD,UCC,
     &            CC2,RCCD,DRCCD

      COMMON/METH/MBPT2,MBPT3,M4DQ,M4SDQ,M4SDTQ,CCD,QCISD,CCSD,UCC,
     &            CC2,RCCD,DRCCD

      DO ILIST = 54,59,2-IUHF
         CALL ZERSYM(WORK,ILIST) 
      ENDDO 

      IF (DRCCD) THEN 
         C4 = -0.50D0
         IF (IUHF .NE. 0) THEN
            CALL DRCC_DWMBEJ(WORK,MAXCOR,'AAAA',IUHF,C4)
            CALL DRCC_DWMBEJ(WORK,MAXCOR,'BBBB',IUHF,C4)
            CALL DRCC_DWMBEJ(WORK,MAXCOR,'BABA',IUHF,C4)
         ENDIF
        CALL DRCC_DWMBEJ(WORK,MAXCOR,'ABAB',IUHF,C4)

      ELSEIF (RCCD) THEN
         C4 = -0.50D0
         IF (IUHF .NE. 0) THEN
            CALL RCC_DWMBEJ(WORK,MAXCOR,'AAAA',IUHF,C4)
            CALL RCC_DWMBEJ(WORK,MAXCOR,'BBBB',IUHF,C4)
            CALL RCC_DWMBEJ(WORK,MAXCOR,'BABA',IUHF,C4)
         ENDIF
        CALL RCC_DWMBEJ(WORK,MAXCOR,'ABAB',IUHF,C4)
      ENDIF 
      RETURN
      END
