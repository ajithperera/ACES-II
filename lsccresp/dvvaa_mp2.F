        SUBROUTINE DVVAA_MP2(T2AMPS,T2,L2,T,DVV,FACT,ISPIN,POP,
     &                       VRT,DISSYT,NUMSYT,NVRT2SQ,T2LEN,
     &                       IRREP,IUHF)

      IMPLICIT DOUBLE PRECISION (A-H,O-Z)
      DOUBLE PRECISION L2 
      INTEGER DISSYT,DIRPRD,POP,VRT,POP1,VRT1
      INTEGER T2LEN
C
      COMMON/MACHSP/IINTLN,IFLTLN,IINTFP,IALONE,IBITWD
      COMMON /SYMINF/ NSTART,NIRREP,IRREPA(255),IRREPB(255),DIRPRD(8,8)
      COMMON/SYM/POP1(8,2),VRT1(8,2),NTAA,NTBB,NF1(2),NF2(2)

      DIMENSION T(DISSYT,NUMSYT)
      DIMENSION T2(NUMSYT,NVRT2SQ)
      DIMENSION L2(NUMSYT,NVRT2SQ),POP(8),VRT(8)
      DIMENSION T2AMPS(T2LEN) 
      DIMENSION DVV(NF2(1)+IUHF*NF2(2))
C
      DATA ONE,ONEM,TWO /1.D0,-1.D0,2.D0/

      CALL DCOPY(NUMSYT*DISSYT,T2AMPS,1,T,1)
      CALL TRANSP(T,T2,NUMSYT,DISSYT)
      CALL DCOPY(NUMSYT*DISSYT,T2,1,L2,1)

      CALL SYMEXP(IRREP,VRT,NUMSYT,L2)
      CALL SYMEXP(IRREP,VRT,NUMSYT,T2)

      IOFF=1
      JOFF=1
      DO IRREPJ=1,NIRREP

         NVRTJ =VRT(IRREPJ)
         IRREPI=DIRPRD(IRREP,IRREPJ)
         NVRTI =VRT(IRREPI)
         IF(MIN(NVRTI,NVRTJ).NE.0) THEN
C
            CALL XGEMM('T','N',NVRTJ,NVRTJ,NVRTI*NUMSYT,FACT,
     &                  T2(1,JOFF),NVRTI*NUMSYT,L2(1,JOFF),
     &                  NVRTI*NUMSYT,ONE,DVV(IOFF),NVRTJ)
         ENDIF

         JOFF=JOFF+NVRTI*NVRTJ
         IOFF=IOFF+NVRTJ*NVRTJ

      ENDDO 
C
      RETURN
      END
