      SUBROUTINE SPN_ADAPT_T1RING(ICORE,MAXCOR,IUHF,LISTA,
     &                            LISTB,LISTC)
C
C THIS SUBROUTINE FORMS THE ALL ALPHA W(mbej) INTERMEDIATE
C  FROM THE ABAB AND ABBA PIECES FOR RHF REFERENCE FUNCTIONS.
C
      IMPLICIT INTEGER (A-Z)
      DOUBLE PRECISION ONEM
      DIMENSION ICORE(MAXCOR)
      COMMON /MACHSP/ IINTLN,IFLTLN,IINTFP,IALONE,IBITWD
      COMMON /SYMPOP/ IRPDPD(8,22),ISYTYP(2,500),ID(18)
      DATA ONEM /-1.0/
      IF(IUHF.NE.0)THEN
       WRITE(6,100)
100    FORMAT(T3,'@QUIKAB, Fatal logic error.  QUIKAB called for UHF.')
      ENDIF
      TOTSIZA=ISYMSZ(ISYTYP(1,LISTA),ISYTYP(1,LISTA))
      TOTSIZB=ISYMSZ(ISYTYP(1,LISTB),ISYTYP(1,LISTB))
      TOTSIZC=ISYMSZ(ISYTYP(1,LISTC),ISYTYP(1,LISTC))
      I000=1
      I010=I000+TOTSIZA*IINTFP
      I020=I010+TOTSIZB*IINTFP 
      IF(I020.GT.MAXCOR)CALL INSMEM('QUIKAA',I020,MAXCOR)
      CALL GETALL(ICORE(I000),TOTSIZA,1,LISTA)
      CALL SSCAL(TOTSIZA,2.0D0,ICORE(I000),1)
      CALL GETALL(ICORE(I010),TOTSIZB,1,LISTB)
      CALL SAXPY(TOTSIZC,ONEM,ICORE(I010),1,ICORE(I000),1)
      CALL PUTALL(ICORE(I000),TOTSIZC,1,LISTC)
      RETURN
      END
