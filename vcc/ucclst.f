      SUBROUTINE UCCLST(ICORE,MAXCOR,IUHF)
C
C DRIVER FOR POINTER CREATION FOR LISTS 151-153 (V(MN,IJ)) AND 
C LISTS 191 AND 192 (G(A,E) AND G(M,I)) AND
C LISTS 114-116 (X(MN,IJ))
C
CEND
      IMPLICIT INTEGER (A-Z)
      DIMENSION ICORE(MAXCOR)
      COMMON /SYMINF/ NSTART,NIRREP,IRREPA(255),IRREPB(255),
     &                DIRPRD(8,8)
      COMMON/SYM/POP(8,2),VRT(8,2),NTAA,NTBB,NF1AA,NF1BB,NF2AA,
     &           NF2BB
c&ps 1 line mod
c      IMODE=1
      call aces_io_remove(51,'GAMLAM')
      IMODE=0
      IF(IUHF.NE.0)CALL INIPCK(1,3,3,151,IMODE,0,1)
      IF(IUHF.NE.0)CALL INIPCK(1,4,4,152,IMODE,0,1)
      CALL INIPCK(1,14,14,153,IMODE,0,1)
c      CALL INIPCK(1,9,9,154,IMODE,1)
c      IF(IUHF.NE.0)CALL INIPCK(1,10,10,155,IMODE,1)
c      CALL INIPCK(1,9,10,156,IMODE,1)
c      IF(IUHF.NE.0)CALL INIPCK(1,10,9,157,IMODE,1)
c      CALL INIPCK(1,11,11,158,IMODE,1)
c      IF(IUHF.NE.0)CALL INIPCK(1,12,12,159,IMODE,0) 
      CALL UPDMOI(1,NF1AA,1,191,IMODE,0)
      CALL UPDMOI(1,NF2AA,1,192,IMODE,0)
      IF(IUHF.NE.0) THEN
       CALL UPDMOI(1,NF1BB,2,191,IMODE,0)
       CALL UPDMOI(1,NF2BB,2,192,IMODE,0)
      ENDIF
c&ps 5 line add
      CALL INIPCK(1,1,3,114,IMODE,0,1)
      CALL INIPCK(1,13,14,116,IMODE,0,1)
      IF(IUHF.EQ.1) THEN
         CALL INIPCK(1,2,4,115,IMODE,0,1)
      ENDIF
      RETURN
      END
