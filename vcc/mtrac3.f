      SUBROUTINE MTRAC3(F,Z,DISSYZ,POP2,POP1,IRREP)
C
C  THIS ROUTINE CALCULATES THE CONTRACTION
C
C   F(m,i) = F(m,i) + SUM N Z(Nm,Ni) 
C
C  THIS CONTRIBUTION IS REQUIRED IN CCSD FOR THE
C  CONSTRUCTION OF THE F(MI) INTERMEDIATES
C
CEND
C
C  CODED JULY/90  JG
C  MODIFIED OCT/92 PS
C
      IMPLICIT DOUBLE PRECISION (A-H,O-Z)
      INTEGER DISSYZ,DIRPRD,POP1,POP2
      DIMENSION F(1),Z(DISSYZ,1),POP1(8),POP2(8)
C
      COMMON /SYMINF/ NSTART,NIRREP,IRREPA(255),IRREPB(255),
     &                DIRPRD(8,8)
      DATA ONE /1.D0/
      INDF=0
      IOFFZ=0
      DO 100 IRREPJ=1,NIRREP
       NOCCJ=POP2(IRREPJ)
       IRREPI=DIRPRD(IRREP,IRREPJ)
       NOCCI=POP1(IRREPI)
       DO 20 I=1,NOCCJ 
       DO 20 M=1,NOCCJ
       INDF=INDF+1
       CALL PSADD(Z(IOFFZ+(M-1)*NOCCI+1,IOFFZ+(I-1)*NOCCI+1),ONE,
     $      DISSYZ+1,F(INDF),NOCCI)
20     CONTINUE
       IOFFZ=IOFFZ+NOCCI*NOCCJ
100   CONTINUE
      RETURN
      END
