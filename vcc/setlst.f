

      SUBROUTINE SETLST(ICORE,MAXCOR,IUHF)
      IMPLICIT INTEGER (A-Z)
      DIMENSION ICORE(MAXCOR)
      COMMON /SYMINF/ NSTART,NIRREP,IRREPA(255),IRREPB(255),
     &                DIRPRD(8,8)
      COMMON/SYM/POP(8,2),VRT(8,2),NTAA,NTBB,NF1AA,NF1BB,NF2AA,
     &           NF2BB
C
C DRIVER FOR POINTER CREATION FOR LISTS 51-59 (RING INTERMEDIATES)
C
      IMODE=0
      IF(IUHF.NE.0)CALL INIPCK(1,3,3,51,IMODE,0,1)
      IF(IUHF.NE.0)CALL INIPCK(1,4,4,52,IMODE,0,1)
      CALL INIPCK(1,14,14,53,IMODE,0,1)
      CALL INIPCK(1,9,9,54,IMODE,0,1)
      IF(IUHF.NE.0)CALL INIPCK(1,10,10,55,IMODE,0,1)
      CALL INIPCK(1,9,10,56,IMODE,0,1)
      IF(IUHF.NE.0)CALL INIPCK(1,10,9,57,IMODE,0,1)
      CALL INIPCK(1,11,11,58,IMODE,0,1)
      IF(IUHF.NE.0)CALL INIPCK(1,12,12,59,IMODE,0,1)
      RETURN
      END
