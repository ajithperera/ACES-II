      SUBROUTINE TPDIJAB(ICORE,MAXCOR,IUHF,LISTL1,LISTL1OFF,LISTL2,
     &   LISTL2RS,LISTR1,LISTR1OFF,LISTR2,LISTR2RS,ZNORM)
      IMPLICIT DOUBLE PRECISION(A-H,O-Z)
      LOGICAL MBPT2,CC
      double precision l0
      LOGICAL SS, SD, DS, DD
      DIMENSION ICORE(MAXCOR)
C
      COMMON/REFTYPE/MBPT2,CC
      COMMON/DRVHBAR/SS, SD, DS, DD
C
C DRIVER PROGRAM FOR THE G(IJ,AB) TERMS)
C
CEND
C
C CODED OCTOBER/93 JG
C
C
C IJAB CONTRIBUTION ZERO
C R fully contracted
c      CALL TPDIJAB0(ICORE,MAXCOR,IUHF,ZNORM)
      L0 = 1.0d0
      CALL TPDIJAB0_R(ICORE,MAXCOR,IUHF,L0,LISTR1,LISTR1OFF,LISTR2)
C
C IJAB CONTRIBUTIONS 1 TO 5
C
      IF (CC) CALL TPDIJAB1(ICORE,MAXCOR,IUHF,LISTL1,LISTL1OFF,LISTL2,
     &   LISTL2RS,LISTR1,LISTR1OFF,LISTR2,LISTR2RS)
C
      CALL TPDIJAB2(ICORE,MAXCOR,IUHF,LISTL1,LISTL1OFF,LISTL2,LISTR1,
     &   LISTR1OFF)
C
      CALL TPDIJAB3(ICORE,MAXCOR,IUHF,LISTL1,LISTL1OFF,LISTL2,LISTL2RS,
     &   LISTR1,LISTR1OFF,LISTR2)
C
      IF (CC .OR. DD)
     &   CALL TPDIJAB4(ICORE,MAXCOR,IUHF,LISTL2,LISTR1,LISTR1OFF,LISTR2)
C
      CALL TPDIJAB5(ICORE,MAXCOR,IUHF,LISTR1,LISTR1OFF,LISTR2RS)
C
C ALL DONE, RETURN
C
      RETURN
      END
