      SUBROUTINE R1L1Y1A(L1,R1,Y1,ICORE,MAXCOR,IUHF)
C
C Y1(a,i) = L(f,i)*R(me)*W(ae,fm)
C
CEND
      IMPLICIT INTEGER (A-Z)
      DOUBLE PRECISION ONE,ZILCH
      DIMENSION ICORE(MAXCOR),R1(*),L1(*),Y1(*),I0X(2)
      COMMON/STATSYM/IRREPX
      COMMON/SYMINF/NSTART,NIRREP,IRREPS(255,2),DIRPRD(8,8)
      COMMON/SYMPOP/IRPDPD(8,22),ISYTYP(2,500),ID(18)
      COMMON/SYM/POP(8,2),VRT(8,2),NT(2),NFMI(2),NFEA(2)
      COMMON/MACHSP/IINTLN,IFLTLN,IINTFP,IALONE,IBITWD
      COMMON/SYMLOC/ISYMOFF(8,8,25)
C
      DATA ONE,ZILCH/1.0D0,0.0D0/
C
C FORM INTERMEDIATE X(af) = R(em)*W(ae,fm)
C
      I0X(1)=1
      IF(IUHF.NE.0)THEN
       I0X(2)=I0X(1)+IINTFP*IRPDPD(IRREPX,19)
      ELSE
       I0X(2)=I0X(1)
      ENDIF
      I000=I0X(2)+IINTFP*IRPDPD(IRREPX,20)
      MXCOR = MAXCOR - I000 + 1
      LENAB=IRPDPD(IRREPX,19)+IUHF*IRPDPD(IRREPX,20)
      CALL ZERO(ICORE(I0X(1)),LENAB)
      CALL DVOINVV(IRREPX,ICORE(I0X(1)),R1,ICORE(I000),MXCOR,IUHF,27)

      IOFFL=1
      IOFFY=1
      DO 10 ISPIN=1,1+IUHF
       IOFFX = I0X(ISPIN)
C
C FORM PRODUCT Y1(ai) = X(af)*L(fi)
C
       DO 11 IRREP=1,NIRREP
        NUMA=VRT(IRREP,ISPIN)
        NUMI=POP(IRREP,ISPIN)
        CALL XGEMM('N','N',NUMA,NUMI,NUMA,ONE,ICORE(IOFFX),NUMA,
     &             L1(IOFFL),NUMA,ONE,Y1(IOFFY),NUMA)
        IOFFL=IOFFL+IINTFP*NUMA*NUMI
        IOFFX=IOFFX+IINTFP*NUMA*NUMA
        IOFFY=IOFFY+IINTFP*NUMA*NUMI
11     CONTINUE
C
10    CONTINUE
C
      RETURN
      END
