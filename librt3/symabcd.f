      SUBROUTINE SYMABCD(CORE,MAXCOR,IUHF,IMODE)
      IMPLICIT DOUBLE PRECISION (A-H,O-Z)
      INTEGER VRT,POP,DIRPRD,DISSIZ
      DIMENSION CORE(1)
C
      COMMON /MACHSP/ IINTLN,IFLTLN,IINTFP,IALONE,IBITWD
      COMMON /SYMINF/ NSTART,NIRREP,IRREPS(255,2),DIRPRD(8,8)
      COMMON /SYMPOP/ IRPDPD(8,22),ISYTYP(2,500),ID(18)
      COMMON /SYM/    POP(8,2),VRT(8,2),NT1(2),NFMI(2),NFEA(2)
      COMMON /FLAGS/  IFLAGS(100)
      EQUIVALENCE(ICLLVL,IFLAGS( 2))
      EQUIVALENCE(IDRLVL,IFLAGS( 3))
      COMMON /FILES/  LUOUT,MOINTS
C
      DO 10 IRREP=1,NIRREP
C
      DISSIZ = IRPDPD(IRREP,13)
      NDIS   = IRPDPD(IRREP,13)
C
      I000 = 1
      I010 = I000 + DISSIZ * NDIS
      I020 = I010 + DISSIZ
      I030 = I020 + DISSIZ
      I040 = I030 + DISSIZ
      NEED = I040 * IINTFP
      IF(NEED.GT.MAXCOR)THEN
      WRITE(LUOUT,1010) NEED,MAXCOR
 1010 FORMAT(' @SYMABCD-I, Insufficient memory. Need ',I10,' Got ',I10)
      CALL INSMEM('SYMABCD',NEED,MAXCOR)
      ENDIF
C
      CALL GETLST(CORE(I000),1,NDIS,2,IRREP,233)
C
      IF(IMODE.EQ.1)THEN
      CALL SYMTR1(IRREP,VRT(1,1),VRT(1,2),DISSIZ,     CORE(I000),
     1            CORE(I010),CORE(I020),CORE(I030))
      CALL SYMTR3(IRREP,VRT(1,1),VRT(1,2),DISSIZ,NDIS,CORE(I000),
     1            CORE(I010),CORE(I020),CORE(I030))
      ELSE
      CALL SYMTR1(IRREP,VRT(1,2),VRT(1,1),DISSIZ,     CORE(I000),
     1            CORE(I010),CORE(I020),CORE(I030))
      CALL SYMTR3(IRREP,VRT(1,2),VRT(1,1),DISSIZ,NDIS,CORE(I000),
     1            CORE(I010),CORE(I020),CORE(I030))
      ENDIF
C
      CALL PUTLST(CORE(I000),1,NDIS,2,IRREP,233)
   10 CONTINUE
      RETURN
      END
