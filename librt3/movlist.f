      SUBROUTINE MOVLIST(Z,LIST1,LIST2)
      IMPLICIT DOUBLE PRECISION (A-H,O-Z)
      INTEGER DISTSZ,DIRPRD
      DIMENSION Z(1)
C
C     Subroutine to copy contents of LIST1 to LIST2 one distribution at a
C     time. Initially, coded within the PUTLIST/GETLIST system, change to
C     PUTLST/GETLST should be easy.
C
      COMMON /SYMINF/ NSTART,NIRREP,IRREPS(255,2),DIRPRD(8,8)
      COMMON /FILES / LUOUT,MOINTS
      COMMON /AUXIO / DISTSZ(8,100),NDISTS(8,100),INIWRD(8,100),LNPHYR,
     1                NRECS,LUAUX
      COMMON /T3IOOF/ IJKPOS(8,8,8,2),IJKLEN(36,8,4),IJKOFF(36,8,4),
     1                NCOMB(4)
C
cYAU - less output
c      WRITE(LUOUT,1000) LIST1,LIST2
 1000 FORMAT(' MOVLIST-I, Copying list ',I3,' to list ',I3)
C
      DO  30 IRREP=1,NIRREP
C
      IF(DISTSZ(IRREP,LIST1).LE.0.OR.NDISTS(IRREP,LIST1).LE.0) GOTO 30
      IF(DISTSZ(IRREP,LIST1).NE.DISTSZ(IRREP,LIST2).OR.
     1   NDISTS(IRREP,LIST1).NE.NDISTS(IRREP,LIST2))THEN
      WRITE(LUOUT,1010) LIST1,LIST2
 1010 FORMAT(' @MOVLIST-F, LIST1 is not the same type as LIST2 ! ',2I6)
      STOP
      ENDIF
C
      DO  20 IDIST=1,NDISTS(IRREP,LIST1)
      CALL GETLIST(Z,IDIST,1,1,IRREP,LIST1)
      CALL PUTLIST(Z,IDIST,1,1,IRREP,LIST2)
   20 CONTINUE
C
   30 CONTINUE
      RETURN
      END
