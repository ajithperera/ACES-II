      SUBROUTINE MKOFOV(IOFFOV)
      IMPLICIT INTEGER (A-Z)
C
      DIMENSION IOFFOV(8,8,4),LENOV(8,8,4)
C
      COMMON /MACHSP/ IINTLN,IFLTLN,IINTFP,IALONE,IBITWD
      COMMON /SYMINF/ NSTART,NIRREP,IRREPA(255),IRREPB(255),
     1                DIRPRD(8,8)
      COMMON /SYMPOP/ IRPDPD(8,22),ISYTYP(2,500),ID(18)
      COMMON /SYM/    POP(8,2),VRT(8,2),NTAA,NTBB,NF1AA,NF2AA,
     1                NF1BB,NF2BB
      COMMON /INFO/   NOCCO(2),NVRTO(2)
C
C     ROUTINE TO COMPUTE VARIOUS OV OFFSETS.
C
C     #     O,V
C
C     1     A,A
C     2     B,B
C     3     B,A
C     4     A,B
C
      DO   30 ISPIN=1,2
      DO   20 IRPOV=1,NIRREP
      DO   10 IRPV =1,NIRREP
      IRPO = DIRPRD(IRPOV,IRPV)
C
      LENOV(IRPV,IRPOV,ISPIN) =  POP(IRPO,ISPIN) * VRT(IRPV,ISPIN)
C
      IF(IRPV.EQ.1)THEN
      IOFFOV(IRPV,IRPOV,ISPIN) = 0
      ELSE
      IOFFOV(IRPV,IRPOV,ISPIN) = IOFFOV(IRPV-1,IRPOV,ISPIN) + 
     1                            LENOV(IRPV-1,IRPOV,ISPIN)
      ENDIF
   10 CONTINUE
   20 CONTINUE
   30 CONTINUE
C
      DO  120 IRPOV=1,NIRREP
      DO  110 IRPV =1,NIRREP
      IRPO = DIRPRD(IRPOV,IRPV)
C
      LENOV(IRPV,IRPOV,3) =  POP(IRPO,1) * VRT(IRPV,2)
C
      IF(IRPV.EQ.1)THEN
      IOFFOV(IRPV,IRPOV,3) = 0
      ELSE
      IOFFOV(IRPV,IRPOV,3) = IOFFOV(IRPV-1,IRPOV,3) + 
     1                        LENOV(IRPV-1,IRPOV,3)
      ENDIF
  110 CONTINUE
  120 CONTINUE
C
      DO  220 IRPOV=1,NIRREP
      DO  210 IRPV =1,NIRREP
      IRPO = DIRPRD(IRPOV,IRPV)
C
      LENOV(IRPV,IRPOV,4) =  POP(IRPO,2) * VRT(IRPV,1)
C
      IF(IRPV.EQ.1)THEN
      IOFFOV(IRPV,IRPOV,4) = 0
      ELSE
      IOFFOV(IRPV,IRPOV,4) = IOFFOV(IRPV-1,IRPOV,4) + 
     1                        LENOV(IRPV-1,IRPOV,4)
      ENDIF
  210 CONTINUE
  220 CONTINUE
      RETURN
      END
