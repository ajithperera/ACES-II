      SUBROUTINE CONTRACT_W2S2(W2,S2,Q,NROW_W2,NCOL_W2,NROW_S2,NCOL_S2,
     &                         LENTAR,IRREPX,ISPIN,MSPIN)

      IMPLICIT DOUBLE PRECISION(A-H,O-Z)

      DIMENSION W2(NROW_W2*NCOL_W2),S2(NROW_S2*NCOL_S2)
      DIMENSION Q(LENTAR)

      INTEGER POP,VRT,SIRREP,DIRPRD
      COMMON /MACHSP/ IINTLN,IFLTLN,IINTFP,IALONE,IBITWD
      COMMON /SYMINF/ NSTART,NIRREP,IRREPS(255,2),DIRPRD(8,8)
      COMMON /SYM/ POP(8,2),VRT(8,2),NT(2),NFMI(2),NFEA(2)
      COMMON /SYMPOP/ IRPDPD(8,22),ISYTYP(2,500),ID(18)
      COMMON /SINFO/ NS(8), SIRREP

      DATA IONE,ONE /1,1.0D0/
     
C Do the contraction W(NM,EF) * S2(NM,EP) -> Z(F,P)

      IOFFW = IONE 
      IOFFS = IONE
      IOFFQ = IONE
      DO IRREPF = 1, NIRREP

         IRREPE = DIRPRD(IRREPF,IRREPX)
         IRREPN = IRREPE 
         IRREPM = IRREPF
         IRREPP = IRREPM
           
         NROW = VRT(IRREPF,ISPIN)
         NCOL = NS(IRREPP)
         NSUM = NROW_S2*VRT(IRREPE,MSPIN)

        IF (NROW*NCOL*NSUM .NE. 0) THEN
            CALL XGEMM('T','N',NROW,NCOL,NSUM,ONE,W2(IOFFW),NSUM,
     &                 S2(IOFFS),NSUM,ONE,Q(IOFFQ),NROW)
        ENDIF
        IOFFW = IOFFW + NROW*NSUM
        IOFFS = IOFFS + NCOL*NSUM
        IOFFQ = IOFFQ + NROW*NCOL 
      ENDDO 

       
      RETURN
      END
