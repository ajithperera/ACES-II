      SUBROUTINE CONTRACT_T2Q(T2,Q,Z2,NROW_T2,NCOL_T2,NROW_Z2,NCOL_Z2,
     &                         LENTAR,IRREPX,ISPIN,MSPIN)

      IMPLICIT DOUBLE PRECISION(A-H,O-Z)

      DIMENSION T2(NROW_T2*NCOL_T2),Z2(NROW_Z2*NCOL_Z2)
      DIMENSION Q(LENTAR)

      INTEGER POP,VRT,DIRPRD
      COMMON /MACHSP/ IINTLN,IFLTLN,IINTFP,IALONE,IBITWD
      COMMON /SYMINF/ NSTART,NIRREP,IRREPS(255,2),DIRPRD(8,8)
      COMMON /SYM/ POP(8,2),VRT(8,2),NT(2),NFMI(2),NFEA(2)
      COMMON /SYMPOP/ IRPDPD(8,22),ISYTYP(2,500),ID(18)
      COMMON /SINFO/ NS(8), SIRREP

      DATA IONE,ONE,ONEM /1,1.0D0,-1.0D0/
     
C Do the contraction  Z(IJ,AP) = - T2(IJA,F) Q(F,P)

      IOFFT = IONE 
      IOFFZ = IONE
      IOFFQ = IONE
      DO IRREPF = 1, NIRREP
         IRREPA = DIRPRD(IRREPF,IRREPX)
         IRREPP = IRREPF
         IRREPI = IRREPA
         IRREPJ = IRREPF
           
         NROW = NROW_T2*VRT(IRREPA,MSPIN)
         NCOL = NS(IRREPP)
         NSUM = VRT(IRREPF,ISPIN)

         IF (NROW*NCOL*NSUM .NE. 0) THEN
            CALL XGEMM('N','N',NROW,NCOL,NSUM,ONEM,T2(IOFFT),
     $                  NROW,Q(IOFFQ),NSUM,ONE,Z2(IOFFZ),
     $                  NROW)
         ENDIF

         IOFFT = IOFFT + NROW*NSUM
         IOFFZ = IOFFZ + NCOL*NSUM
         IOFFQ = IOFFQ + NROW*NCOL 
      ENDDO 
       
      RETURN
      END
