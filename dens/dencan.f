      SUBROUTINE DENCAN(DENS,SCR,NSIZE,NIRREP,NUM)
C
C  THIS ROUTINE SETS THE OFF-DIAGONAL PART
C  OF THE OCC-OCC (VIRT-VIRT) PART OF THE
C  DENSITY TO ZERO
C
C  USED IN THE CASE OF PERTURBED CANONICAL ORBITALS
C
CEND
C
C CODED 3/91 JG
C
      IMPLICIT DOUBLE PRECISION(A-H,O-Z)
      DIMENSION DENS(1),SCR(1),NUM(8)
C
      COMMON/MACHSP/IINTLN,IFLTLN,IINTFP,IALONE,IBITWD
C
      DATA ONEM,TWO /-1.D0,2.D0/
C
c YAU : old
c     CALL ICOPY(IINTFP*NSIZE,DENS,1,SCR,1)
c YAU : new
      CALL DCOPY(NSIZE,DENS,1,SCR,1)
c YAU : end
C
C  LOOP OVER ALL IRREPS
C
      IOFF=1
      DO 100 IRREP=1,NIRREP
C
       N=NUM(IRREP)
C
       CALL SSCAL(N,TWO,DENS(IOFF),N+1)
C 
       IOFF=IOFF+N*N
C
100   CONTINUE
C
      CALL SAXPY(NSIZE,ONEM,SCR,1,DENS,1)
      RETURN
      END
