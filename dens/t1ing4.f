
      SUBROUTINE T1ING4(G,DISSYG,NUMSYG,T1,FACT,ISPIN1,ISPIN2,
     &                  LIST1,LIST2)
C
C     THIS ROUTINE FORMS THE T1 CONTRIBUTION TO G(IA,JB)
C
C      G(IA,JB) = - 1/2 T(I,B) L(J,A) + 1/2 T(J,A) L(I,B)
C
C     THIS TERM IS REQUIRED ONLY FOR QCISD AND CCSD
C
C     THERE ARE ONLY TWO SPIN CASES TO CONSIDER
C
C       AAAA         AND         ABBA
C
C     NOTE ALSO THAT THIS TERM CONTRIBUTES ONLY TO IRREP ONE 
C
CEND
C
C   CODED AUGUST/90     JG
C
      IMPLICIT DOUBLE PRECISION(A-H,O-Z)
      INTEGER DISSYG
      DIMENSION G(DISSYG,NUMSYG),T1(1)
C
C GET THE T1 AND L1 AMPLITUDES
C
       CALL GETLST(T1,1,1,1,ISPIN1,LIST1)
       CALL GETLST(T1(1+DISSYG),1,1,1,ISPIN2,LIST2)
C
C  CALCULATE THE CONTRIBUTION
C
       DO 100 IDIS=1,NUMSYG
       DO 100 IDSZ=1,DISSYG
       G(IDSZ,IDIS)=G(IDSZ,IDIS)+FACT*T1(IDSZ)*T1(IDIS+DISSYG)
100    CONTINUE
C 
       RETURN
       END
