      SUBROUTINE IO5AA(G,W,T,AIOO,FACT,ISPIN,POP,VRT,DISSYT,NUMSYT,
     &                  DISSYW,NUMSYW,LISTG,LISTW,IRREP,TMP)
      IMPLICIT DOUBLE PRECISION (A-H,O-Z)
      INTEGER DISSYT,DISSYW,DIRPRD,POP,VRT
      DIMENSION G(NUMSYT,1),W(NUMSYW,1),T(DISSYT,1),AIOO(1),
     &          POP(8),VRT(8),TMP(1)
      COMMON /SYMINF/ NSTART,NIRREP,IRREPA(255,2),DIRPRD(8,8)
      common /dropgeo/ ndrgeo
C
      DATA ONE /1.D0/
C
C      PICK UP THE G AMPLITUDES AND INTEGRALS REQUIRED
C      AND TRANSPOSE G AND W
C
      if (ndrgeo.eq.0) CALL GETLST(T,1,NUMSYT,1,IRREP,LISTG)
      if (ndrgeo.eq.1) 
     x      CALL GETGO6U(T,G,1,NUMSYT,1,IRREP,LISTG,ISPIN,listw,dissyt)
      CALL TRANSP(T,G,NUMSYT,DISSYT)
C
      CALL GETLST(T,1,NUMSYW,2,IRREP,LISTW)   
      CALL TRANSP(T,W,NUMSYW,DISSYW)
C
C  EXPAND THE OCCUPIED-OCCUPIED BLOCK OF G AND W
C
      CALL SYMEXP(IRREP,POP,NUMSYT,G)
      CALL SYMEXP(IRREP,POP,NUMSYW,W)
C
C  PERFORM MULTIPLICATION 
C
C  JOFF OFFSET IN THE RIGHTMOST BLOCKS OF G AND W
C  IOFF OFFSET IN IOV
C
      IOFF=1
      JOFF=1
      DO 90 IRREPJ=1,NIRREP
C          
C        GET NUMBER OF OCCUPIED ORBITALS FOR IRREPJ     
C
       NOCCJ=POP(IRREPJ)
C
C        DETERMINE IRREPI WHOSE DIRECT PRODUCT WITH IRREPJ GIVES IRREP
C
       IRREPI=DIRPRD(IRREP,IRREPJ)
C
C        GET NUMBER OF OCCUPIED ORBITALS FOR IRREPI
C
       NOCCI=POP(IRREPI)
C
C        IF NOCCJ OR NOCCI EQUAL ZERO, NOTHING TO COMPUTE
C
       IF(MIN(NOCCI,NOCCJ).NE.0) THEN
C
          CALL XGEMM('T','N',NOCCJ,NOCCJ,NOCCI*NUMSYT,FACT,
     &             W(1,JOFF),NOCCI*NUMSYT,G(1,JOFF),
     &             NOCCI*NUMSYT,ONE,AIOO(IOFF),NOCCJ)
       ENDIF
C
C  UPDATE OFFSETS
C
       JOFF=JOFF+NOCCI*NOCCJ
       IOFF=IOFF+NOCCJ*NOCCJ
90    CONTINUE
C
      RETURN
      END
