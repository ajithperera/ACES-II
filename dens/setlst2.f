      SUBROUTINE SETLST2(ICORE,MAXCOR,IUHF)
      IMPLICIT INTEGER (A-Z)
      DIMENSION ICORE(MAXCOR)
      COMMON/SYMINF/NSTART,NIRREP,IRREPS(255,2),DIRPRD(8,8)
      COMMON/SYM/POP(8,2),VRT(8,2),NT(2),NF1(2),NF2(2)
C
C DRIVER FOR POINTER CREATION FOR LISTS 254-259 (RING INTERMEDIATES)
C
      IMODE=0
      CALL INIPCK(1,9,9,254,IMODE,0,1)
      IF(IUHF.NE.0)CALL INIPCK(1,10,10,255,IMODE,0,1)
      CALL INIPCK(1,9,10,256,IMODE,0,1)
      IF(IUHF.NE.0)CALL INIPCK(1,10,9,257,IMODE,0,1)
      CALL INIPCK(1,11,11,258,IMODE,0,1)
      IF(IUHF.NE.0)CALL INIPCK(1,12,12,259,IMODE,0,1)
C
      CALL INIPCK(1,13,14,216,IMODE,0,1)
      IF(IUHF.EQ.0) THEN
       CALL INIPCK(1,1,3,215,IMODE,0,1)
       CALL INIPCK(1,2,4,214,IMODE,0,1)
      ENDIF
C
      RETURN
      END
