         
      SUBROUTINE SDINVVC(IRREPX,IVV,DOO,ICORE,MXCOR,IUHF)
C
C THIS ROUTINE COMPUTES THE CONTRIBUTIONS:
C
C      FD(A,B) = - SUM SD(N,M) * (<AM||BN> - <AN||BM>)        (ISPIN=1)
C
C      FD(a,b) = - SUM SD(n,m) * (<am||bn> - <an||bm>)        (ISPIN=2)
C
C NOTE THAT THIS ROUTINE DEALS WITH COMPLEX PERTURBATION AND THAT
C THEREFORE SD(M,N) IS ANTISYMMETRIC
C
CEND
C
C  HACKED UP FROM SDINVV SEP/91 JG UNIVERSITY OF KARLSRUHE
C
      IMPLICIT INTEGER (A-Z)
      CHARACTER*4 TYPE
      DOUBLE PRECISION IVV,DOO,ONE
      DIMENSION DOO(1),IVV(1),ICORE(MXCOR)
      COMMON /SYM/ POP(8,2),VRT(8,2),NT(2),NFMI(2),NFEA(2)
      COMMON /SYMINF/ NSTART,NIRREP,IRREPS(255,2),DIRPRD(8,8)
      COMMON /SYMPOP/ IRPDPD(8,22),ISYTYP(2,500),ID(18)
      COMMON /MACHSP/ IINTLN,IFLTLN,IINTFP,IALONE,IBITWD
C
      DATA ONE /1.0D0/
C
      IF(IUHF.EQ.0)THEN
C
C SPIN ADAPTED RHF CODE
C
C  FOR CLOSED SHELL WE GET :  -S(M,N) <Am||Nb>  = S(M,N) <Ab||Mn>
C
       LISTW=16
C
       DISSIZ=IRPDPD(IRREPX,ISYTYP(1,LISTW)) 
       NUMDIS=IRPDPD(IRREPX,ISYTYP(2,LISTW)) 
       I000=1
       I010=I000+IINTFP*NUMDIS*DISSIZ
C
       CALL GETLST(ICORE(I000),1,NUMDIS,1,IRREPX,LISTW)
       CALL XGEMM('N','N',DISSIZ,1,NUMDIS,ONE,ICORE(I000),
     &            DISSIZ,DOO,NUMDIS,ONE,IVV,DISSIZ)
C
120    CONTINUE
C
      ELSEIF(IUHF.NE.0)THEN
C
       call errex
C
C    check code and correct !
C
       DO 10 ISPIN=1,2
C
        LISTW=22+ISPIN
C
        DO 20 IRREP=1,NIRREP 
C
         NDSZ1=IRPDPD(IRREP,ISYTYP(1,LISTW))
         NDIS1=IRPDPD(IRREP,ISYTYP(2,LISTW))
         IOFFI=1+(ISPIN-1)*IRPDPD(IRREPX,19)
C
C DO  SD(m,n) * I(ma,nb)   -->    SD(m,n) * - I(na,mb)
C
         I000=1
         I010=I000+IINTFP*NDSZ1*NDIS1
         I020=I010+IINTFP*IRPDPD(IRREPX,19)
         IOFFD=1+(ISPIN-1)*IRPDPD(IRREPX,21)
C
         CALL GETLST(ICORE(I000),1,NDIS1,2,IRREP,LISTW1)
         CALL VMINUS(ICORE(I000),NDSZ1*NDIS1)
         CALL DDOT24(IRREPX,IRREP,IVV(IOFFI),DOO(IOFFD),ICORE(I000),
     &              ICORE(I010),NDSZ1,VRT(1,ISPIN),VRT(1,ISPIN),
     &              VRT(1,ISPIN),POP(1,ISPIN),VRT(1,ISPIN),POP(1,ISPIN),
     &             'TSTS')
C
20      CONTINUE
C
10     CONTINUE
C
      ENDIF
C
      RETURN
      END
