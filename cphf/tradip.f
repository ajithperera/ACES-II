      SUBROUTINE TRADIP(SDIPDR,CDIPDR,TRSTOC,NCOOR)
      IMPLICIT DOUBLE PRECISION (A-H,O-Z)
      PARAMETER (D1 = 1.0D0)
      DIMENSION SDIPDR(3,1),CDIPDR(3,1),TRSTOC(1)
C
      COMMON/MACHSP/IINTLN,IFLTLN,IINTFP,IALONE,IBITWD
      COMMON/PERT/NTPERT,NPERT(8),IPERT(8),IXYZ(3),IJUNK(7)
C
      DATA AZERO,ONE /0.0D0,1.D0/
C
      CALL GETREC(20,'JOBARC','SYMCOORD',IINTFP*NCOOR*NCOOR,
     *            TRSTOC)
C
      CALL XGEMM('N','T',3,NCOOR,NCOOR,ONE,SDIPDR,3,
     *           TRSTOC,NCOOR,AZERO,CDIPDR,3)
C
      RETURN
      END
