      SUBROUTINE SDINOOC(IRREPX,IOO,DOO,ICORE,MXCOR,IUHF)
C
C THIS ROUTINE COMPUTES THE CONTRIBUTIONS:
C
C      FD(I,J) = - 1/2 SUM SD(N,M) (<IM||JN> - <IN||JM>) (ISPIN=1)
C
C      FD(i,j) = - 1/2 SUM SD(n,m) (<im||jn> - <in||jm>) (ISPIN=2)
C
C NOTE THAT THIS ROUTINE DEALS WITH COMPLEX PERTURBATIONS AND
C THAT IN THIS CASE THE COMPLEX CONJUGATE IS THE NEGATIVE OF
C THE ORIGINAL ( SD IS AN ANTISYMMETRIC MATRIX). THERE IS
C ALSO NO SPIN COUPLING.
C
CEND
C
C  HACKED UP FROM SDINOO, SEPT/91 JG UNIVERSITY OF KARLSRUHE
C
      IMPLICIT INTEGER (A-Z)
      CHARACTER*4 TYPE
      DOUBLE PRECISION IOO,DOO,ONE,ONEM
      DIMENSION DOO(1),IOO(1),ICORE(MXCOR)
      COMMON /SYM/ POP(8,2),VRT(8,2),NT(2),NFMI(2),NFEA(2)
      COMMON /SYMINF/ NSTART,NIRREP,IRREPS(255,2),DIRPRD(8,8)
      COMMON /SYMPOP/ IRPDPD(8,22),ISYTYP(2,500),ID(18)
      COMMON /MACHSP/ IINTLN,IFLTLN,IINTFP,IALONE,IBITWD
C
      DATA ONE,ONEM /1.D0,-1.D0/
C
      IF(IUHF.EQ.0)THEN
C
C SPIN ADAPTED RHF CODE
C
C FOR CLOSED SHELL, THIS TERM REDUCES TO :   SUM SD(M,N) <Ij||Mn>
C
       LISTW=13
C
C  LOOP OVER IRREPS
C
       DISSIZ=IRPDPD(IRREPX,ISYTYP(1,LISTW))
       NUMDIS=IRPDPD(IRREPX,ISYTYP(2,LISTW))
C
       I000=1
       I010=I000+IINTFP*NUMDIS*DISSIZ
C
       CALL GETLST(ICORE(I000),1,NUMDIS,1,IRREPX,LISTW)
       CALL XGEMM('N','N',DISSIZ,1,NUMDIS,ONE,ICORE(I000),
     &            DISSIZ,DOO,NUMDIS,ONE,IOO,DISSIZ)
C
      ELSEIF(IUHF.NE.0)THEN 
C
       call errex
C
C   check code and correct
C
C UHF CODE
C
C   LOOP OVER SPIN CASES
C
       DO 10 ISPIN=1,2
C
        LISTW1=10+ISPIN
C
C     LOOP OVER IRREPS
C
        DO 20 IRREP=1,NIRREP
C
         NDSZ1=IRPDPD(IRREP,ISYTYP(1,LISTW1))
         NDSZF=IRPDPD(IRREP,20+ISPIN)
         NDIS1=IRPDPD(IRREP,ISYTYP(2,LISTW1))
         NDISF=IRPDPD(IRREP,20+ISPIN)
         IOFFI=1+(ISPIN-1)*IRPDPD(IRREPX,21)
C
C DO   + SD(m,n) * I(im,jn)
C
         I000=1
         I010=I000+IINTFP*NDSZF*NDISF
         I020=I010+IINTFP*IRPDPD(IRREPX,20+ISPIN)
C
         IOFFD=1+(ISPIN-1)*IRPDPD(IRREPX,21)
         CALL GETLST(ICORE(I000),1,NDIS1,2,IRREP,LISTW1)
         CALL SYMEXP(IRREP,POP(1,ISPIN),NDSZ1,ICORE(I000))
         CALL SYMEXP2(IRREP,POP(1,ISPIN),NDSZF,NDSZ1,NDISF,ICORE(I000),
     &                ICORE(I000))
         CALL DDOT24(IRREPX,IRREP,IOO(IOFFI),DOO(IOFFD),ICORE(I000),
     &              ICORE(I010),NDSZF,POP(1,ISPIN),POP(1,ISPIN),
     &              POP(1,ISPIN),POP(1,ISPIN),POP(1,ISPIN),POP(1,ISPIN),
     &             'STST')
C
20      CONTINUE 
C
10     CONTINUE
C
      ENDIF
C
C ALL DONE
C
      RETURN
      END
