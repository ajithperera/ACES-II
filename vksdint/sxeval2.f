
      SUBROUTINE SXEVAL2(IRREPX,FD,E,SD,POP,VRT)
C
      IMPLICIT DOUBLE PRECISION (A-H,O-Z)
      INTEGER POP,VRT,DIRPRD
      DIMENSION FD(10000),E(100),SD(10000),POP(8),VRT(8)
C
      COMMON/SYMINF/NSTART,NIRREP,IRREPA(255,2),DIRPRD(8,8)
C
      DATA ONEM /-1.0D0/
C
      IOFFD=0
      IOFFER=0
C
      DO 1000 IRREPR=1,NIRREP
C
       IRREPL=DIRPRD(IRREPX,IRREPR)
C
       NR=POP(IRREPR)
       NL=VRT(IRREPL)
C
       DO 100 I=1,NR
C
        CALL SAXPY(NL,ONEM*E(IOFFER+I),SD(IOFFD+(I-1)*NL+1),
     &             1,FD(IOFFD+(I-1)*NL+1),1)
C
100    CONTINUE
C
       IOFFD=IOFFD+NR*NL
       IOFFER=IOFFER+NR
C
1000  CONTINUE
C
      RETURN
      END
