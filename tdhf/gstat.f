      SUBROUTINE GSTAT(F1,F2,E1,E2,U1,U2,LCOMP,NCOMP,NSIZ1,NSIZ3,NSIZO)
      IMPLICIT DOUBLE PRECISION (A-H,O-Z)
      CHARACTER*1 AXYZ 
      COMMON/INFOA/NBASIS,NUMSCF,NX,NMO2,NOC,NVT,NVO
      COMMON/CONST/ EBETA,EGAMMA 
      DIMENSION U1(NSIZ1,NSIZO,3),U2(NSIZ1,NSIZO,6)
      DIMENSION F1(NSIZ1,NSIZ1,3),F2(NSIZ1,NSIZ1,6),E1(NSIZO,NSIZO,3)
     X ,E2(NSIZO,NSIZO,6)
      DIMENSION LCOMP(21,4),ICOMP(6),JCOMP(6),IJCP(3,3),AXYZ(3)
      DATA ZERO/0.D00/,TWO/2.D0/,HALF/5.D-01/,FOUR/4.D0/
      DATA ONE/1.D00/,OCTH/1.D-8/,THREE/3.D0/,SIX/6.D0/           
      DATA ICOMP/ 1,2,3,1,2,3/, JCOMP/ 1,2,3,2,3,1/,IJCP/ 1,4,6,
     X 4,2,5, 6,5,3 /,AXYZ/ 'x','y','z'/                 
      WRITE(6,*) ' Now in STAT ' 
      WRITE(6,*) ' I J K L and GANMA(U1U2,U1U1) '
      GPARA=ZERO
      GPERP=ZERO
      DO 50 LCOM=1,NCOMP
      U1U2F1=ZERO
      U1U2F2=ZERO
      U1U2E1=ZERO
      U1U2E2=ZERO
      U1U1F=ZERO
      U1U1E=ZERO
C     GZZZZ=ZERO
      L1 = LCOMP(LCOM,1)
      L2 = LCOMP(LCOM,2)
      L3 = LCOMP(LCOM,3)
      L4 = LCOMP(LCOM,4)
      DO 10 I=1,NUMSCF               
      DO 10 J=1,NUMSCF                   
      DO 10 K=1,NOC
      U1U2F1 = U1U2F1 + U1(I,K,L1)*F1(I,J,L2)*U2(J,K,IJCP(L3,L4))
     X + U2(I,K,IJCP(L3,L4))*F1(I,J,L2)*U1(J,K,L1)
     X + U1(I,K,L1)*F1(I,J,L3)*U2(J,K,IJCP(L2,L4))
     X + U2(I,K,IJCP(L2,L4))*F1(I,J,L3)*U1(J,K,L1)
     X + U1(I,K,L1)*F1(I,J,L4)*U2(J,K,IJCP(L2,L3))
     X + U2(I,K,IJCP(L2,L3))*F1(I,J,L4)*U1(J,K,L1)
C
      U1U2F2=U1U2F2 +  U1(I,K,L2)*F1(I,J,L1)*U2(J,K,IJCP(L3,L4))
     X + U2(I,K,IJCP(L3,L4))*F1(I,J,L1)*U1(J,K,L2)
     X + U1(I,K,L3)*F1(I,J,L1)*U2(J,K,IJCP(L2,L4))
     X + U2(I,K,IJCP(L2,L4))*F1(I,J,L1)*U1(J,K,L3)
     X + U1(I,K,L4)*F1(I,J,L1)*U2(J,K,IJCP(L2,L3))
     X + U2(I,K,IJCP(L2,L3))*F1(I,J,L1)*U1(J,K,L4)
      U1U1F = U1U1F + U1(I,K,L1)*F2(I,J,IJCP(L3,L4))*U1(J,K,L2)
     X + U1(I,K,L2)*F2(I,J,IJCP(L3,L4))*U1(J,K,L1)
     X + U1(I,K,L1)*F2(I,J,IJCP(L2,L4))*U1(J,K,L3)
     X + U1(I,K,L3)*F2(I,J,IJCP(L2,L4))*U1(J,K,L1)
     X + U1(I,K,L1)*F2(I,J,IJCP(L2,L3))*U1(J,K,L4)
     X + U1(I,K,L4)*F2(I,J,IJCP(L2,L3))*U1(J,K,L1)
C     GZZZZ= GZZZZ+U1(I,K,3)*F1(I,J,3)*U2(J,K,3)*TWO
C    X            +U1(I,K,3)*F2(I,J,3)*U1(J,K,3) 
   10 CONTINUE
      DO 11 K=1,NOC
      DO 11 L=1,NOC
      DO 11 I=1,NUMSCF               
      U1U2E1 = U1U2E1 - U1(I,K,L1)*U2(I,L,IJCP(L3,L4))*E1(K,L,L2)
     X  - U2(I,K,IJCP(L3,L4))*U1(I,L,L1)*E1(K,L,L2)
     X  - U1(I,K,L1)*U2(I,L,IJCP(L2,L4))*E1(K,L,L3)
     X  - U2(I,K,IJCP(L2,L4))*U1(I,L,L1)*E1(K,L,L3)
     X  - U1(I,K,L1)*U2(I,L,IJCP(L2,L3))*E1(K,L,L4)
     X  - U2(I,K,IJCP(L2,L3))*U1(I,L,L1)*E1(K,L,L4)
C
      U1U2E2 = U1U2E2 - U1(I,K,L2)*U2(I,L,IJCP(L3,L4))*E1(K,L,L1)
     X  - U2(I,K,IJCP(L3,L4))*U1(I,L,L2)*E1(K,L,L1)
     X  - U1(I,K,L3)*U2(I,L,IJCP(L2,L4))*E1(K,L,L1)
     X  - U2(I,K,IJCP(L2,L4))*U1(I,L,L3)*E1(K,L,L1)
     X  - U1(I,K,L4)*U2(I,L,IJCP(L2,L3))*E1(K,L,L1)
     X  - U2(I,K,IJCP(L2,L3))*U1(I,L,L4)*E1(K,L,L1)
      U1U1E = U1U1E - U1(I,K,L1)*U1(I,L,L2)*E2(K,L,IJCP(L3,L4))
     X - U1(I,K,L2)*U1(I,L,L1)*E2(K,L,IJCP(L3,L4))
     X - U1(I,K,L1)*U1(I,L,L3)*E2(K,L,IJCP(L2,L4))
     X - U1(I,K,L3)*U1(I,L,L1)*E2(K,L,IJCP(L2,L4))
     X - U1(I,K,L1)*U1(I,L,L4)*E2(K,L,IJCP(L2,L3))
     X - U1(I,K,L4)*U1(I,L,L1)*E2(K,L,IJCP(L2,L3))
C     GZZZZ = GZZZZ-U1(I,K,3)*U2(I,L,3)*E1(K,L,3)*TWO
C    X             -U1(I,K,3)*U1(I,L,3)*E2(K,L,3)
   11 CONTINUE
C  .. two;occupation
      GANMA = -(U1U2F1+U1U2F2+U1U1F+U1U2E1+U1U2E2+U1U1E)*TWO
      GPARA = GPARA + GANMA
      IF(LCOM.EQ.1.OR.LCOM.EQ.2.OR.LCOM.EQ.3) GPARA=GPARA+GANMA+GANMA
      IF(LCOM.EQ.1.OR.LCOM.EQ.2.OR.LCOM.EQ.3) THEN
      GPERP=GPERP+GANMA
      ELSE
      IF(LCOM.EQ.6.OR.LCOM.EQ.7.OR.LCOM.EQ.12.OR.LCOM.EQ.13.OR.LCOM
     X .EQ.18.OR.LCOM.EQ.19)  THEN
      GPERP=GPERP+GANMA*TWO
      ELSE
      GPERP = GPERP - GANMA*HALF
      ENDIF
      ENDIF
C     WRITE(6,*) L1,',',L2,',',L3,',',L4,' gamma = ',GANMA
      WRITE(6,20) AXYZ(L1),AXYZ(L2),AXYZ(L3),AXYZ(L4),GANMA
   20 FORMAT(1H ,4A1,' = ',F20.5)
C     WRITE(6,*) U1U2F1,U1U2F2,U1U1F,U1U2E1,U1U2E2,U1U1E
   50 CONTINUE
C     GZZZZ = -GZZZZ*SIX*TWO
C     WRITE(6,*) ' gamma(z,z,z,z) = ',GZZZZ
      GPARA= GPARA/15
      GPERP= GPERP/15
      WRITE(6,*) ' Parallel and Perpondecular (a.u.) '
      WRITE(6,*) GPARA,GPERP
      GPARAE = GPARA*EGAMMA
      GPERPE = GPERP*EGAMMA
      WRITE(6,*) '  in 10**-39 e.s.u. '
      WRITE(6,*) GPARAE,GPERPE
      RETURN
      END
