      SUBROUTINE G4AB41(GAMMA,BUF,GOFF,DOIRR,LENGAM,FAIBJ)
C
C THIS ROUTINE PROCESSES THE G(Ai,Bj) GAMMA LISTS AND FILLS
C  OUT THE ABCD SYMMETRY CASE MULLIKEN ORDERED GAMMA.
C
C FOR THIS, THE FOLLOWING SYMMETRIZATION IS REQUIRED:
C
C         12 34      12 34      21 34
C       G(AB,ij) = g(AB,ij) + g(BA,CD)
C
C         13 24      13 24      23 14
C       G<Ai,Bj> = g<Ai,Bj> + g<Bi,Aj>
C
C NOTE THAT ABCD IN MULLIKEN CORRESPONDS TO ACBD IN DIRAC.  NOTE ALSO THAT
C  ALL ELEMENTS OF GAMMA WHICH ARE PROCESSED HERE COME FROM DPD LISTS
C  FOR IRREPS 2-h, SINCE A.NE.C;B.NE.D.
C
CEND
      IMPLICIT INTEGER (A-Z)
      DOUBLE PRECISION GAMMA(LENGAM),BUF(1),X,X1,X2 
      DOUBLE PRECISION FABIJ,FAIBJ,FABCD,FIJKL,FABCI,FIJKA
      INTEGER GOFF(8,8,8,8),IDID(8),IOFFSL(8)
      INTEGER IOFFSR(8),DOIRR(42,4)
      LOGICAL MBPT2,MBPT3,M4DQ,M4SDQ,M4SDTQ,CCD,QCISD,CCSD
      COMMON /METH/ MBPT2,MBPT3,M4DQ,M4SDQ,M4SDTQ,CCD,QCISD,CCSD
      COMMON /MACHSP/ IINTLN,IFLTLN,IINTFP,IALONE,IBITWD
      COMMON /SYMINF/ NSTART,NIRREP,IRREPS(255,2),DIRPRD(8,8)
      COMMON /SYM/ POP(8,2),VRT(8,2),NT(2),NFMI(2),NFEA(2)
      COMMON /SYMPOP/ IRPDPD(8,22),ISYTYP(2,500),ID(18)
      COMMON /MOPOPS/ MOPOP(8)
C
      INDXF(I,J,N)=I+(J-1)*N
      INDXT(I,J)  =I+(J*(J-1))/2
      NNP1O2(I)   =(I*(I+1))/2
C
C CALCULATE HOW MANY UNIQUE ABCD SYMMETRY SETS THERE ARE
C
      IF(NIRREP.EQ.8)THEN
       NUMABCD=42
      ELSEIF(NIRREP.EQ.4)THEN
       NUMABCD=3
      ENDIF
C     
      LISTAIBJ=125
      DO 100 ICOUNT=1,NUMABCD
       IRREP1D=DOIRR(ICOUNT,1)
       IRREP2D=DOIRR(ICOUNT,3)
       IRREP3D=DOIRR(ICOUNT,2)
       IRREP4D=DOIRR(ICOUNT,4)
       IRREP1M=DOIRR(ICOUNT,1)
       IRREP2M=DOIRR(ICOUNT,2)
       IRREP3M=DOIRR(ICOUNT,3)
       IRREP4M=DOIRR(ICOUNT,4)
       IOFFSET=GOFF(IRREP1M,IRREP2M,IRREP3M,IRREP4M)
C
C FIRST DO THE VVOO PART - 1234 PART FIRST
C
       IRREPGET=DIRPRD(IRREP3D,IRREP4D)
C
       IOFFR=1
       IOFFL=1
       DO 2000 IRREPA=1,NIRREP
        IRREPB=DIRPRD(IRREPA,IRREPGET)
        IOFFSR(IRREPA)=IOFFR
        IOFFSL(IRREPA)=IOFFL
        IOFFR=IOFFR+VRT(IRREPB,1)*POP(IRREPA,2)
        IOFFL=IOFFL+VRT(IRREPB,1)*POP(IRREPA,2)
2000   CONTINUE
C
       ILOGREC=IOFFSR(IRREP4D)
       IFIRST=IOFFSL(IRREP2D)
       NUMJ=POP(IRREP4D,2)
       NUMB=VRT(IRREP3D,1)
       NUMI=POP(IRREP2D,2)
       NUMA=VRT(IRREP1D,1) 
       MAXL=MOPOP(IRREP1M)*MOPOP(IRREP2M)
       DO 120 INDXJ=1,NUMJ
        DO 130 INDXB=1,NUMB
         INDJ=INDXJ
         INDB=INDXB+POP(IRREP3D,2)
         CALL GETLST(BUF,ILOGREC,1,1,IRREPGET,LISTAIBJ)
         ILOGREC=ILOGREC+1
         IPOS=IOFFSL(IRREP2D)
         DO 140 INDXI=1,NUMI
          DO 150 INDXA=1,NUMA
           X=BUF(IPOS)*FAIBJ
           IPOS=IPOS+1
           INDA=INDXA+POP(IRREP1D,1)
           INDI=INDXI
           GINDXL=INDXF(INDA,INDB,MOPOP(IRREP1M))
           GINDXR=INDXF(INDI,INDJ,MOPOP(IRREP3M))
           GINDX0=INDXF(GINDXL,GINDXR,MAXL)
           GINDX =GINDX0+IOFFSET
           GAMMA(GINDX)=GAMMA(GINDX)+X
150       CONTINUE
140      CONTINUE
130     CONTINUE
120    CONTINUE
C
C  NOW DO THE OOVV CONTRIBUTION
C
       ILOGREC=IOFFSR(IRREP3D)
       IFIRST=IOFFSL(IRREP1D)
       NUMJ=POP(IRREP3D,2)
       NUMB=VRT(IRREP4D,1)
       NUMI=POP(IRREP1D,2)
       NUMA=VRT(IRREP2D,1) 
       MAXL=MOPOP(IRREP1M)*MOPOP(IRREP2M)
       DO 160 INDXJ=1,NUMJ
        DO 170 INDXB=1,NUMB
         INDJ=INDXJ
         INDB=INDXB+POP(IRREP4D,2)
         CALL GETLST(BUF,ILOGREC,1,1,IRREPGET,LISTAIBJ)
         ILOGREC=ILOGREC+1
         IPOS=IOFFSL(IRREP1D)
         DO 180 INDXI=1,NUMI
          DO 190 INDXA=1,NUMA
           X=BUF(IPOS)*FAIBJ
           IPOS=IPOS+1
           INDA=INDXA+POP(IRREP2D,1)
           INDI=INDXI
           GINDXL=INDXF(INDI,INDJ,MOPOP(IRREP1M))
           GINDXR=INDXF(INDA,INDB,MOPOP(IRREP3M))
           GINDX0=INDXF(GINDXL,GINDXR,MAXL)
           GINDX =GINDX0+IOFFSET
           GAMMA(GINDX)=GAMMA(GINDX)+X
190       CONTINUE
180      CONTINUE
170     CONTINUE
160    CONTINUE
C
C                  23 14  
C NOW PUT IN THE g<Bi,Aj> PIECES
C
       IRREPGET=DIRPRD(IRREP1D,IRREP4D)
       IOFFR=1
       IOFFL=1
C
       DO 1000 IRREPA=1,NIRREP
        IRREPB=DIRPRD(IRREPA,IRREPGET)
        IOFFSR(IRREPA)=IOFFR
        IOFFSL(IRREPA)=IOFFL
        IOFFR=IOFFR+VRT(IRREPB,1)*POP(IRREPA,2)
        IOFFL=IOFFL+VRT(IRREPB,1)*POP(IRREPA,2)
1000   CONTINUE
C
C FIRST DO THE VVOO PART
C
       ILOGREC=IOFFSR(IRREP4D)
       IFIRST=IOFFSL(IRREP2D)
       NUMJ=POP(IRREP4D,2)
       NUMB=VRT(IRREP1D,1)
       NUMI=POP(IRREP2D,2)
       NUMA=VRT(IRREP3D,1) 
       MAXL=MOPOP(IRREP1M)*MOPOP(IRREP2M)
       DO 220 INDXJ=1,NUMJ
        DO 230 INDXB=1,NUMB
         INDJ=INDXJ
         INDB=INDXB+POP(IRREP1D,1)
         CALL GETLST(BUF,ILOGREC,1,1,IRREPGET,LISTAIBJ)
         ILOGREC=ILOGREC+1
         IPOS=IOFFSL(IRREP2D)
         DO 240 INDXI=1,NUMI
          DO 250 INDXA=1,NUMA
           X=BUF(IPOS)*FAIBJ
           IPOS=IPOS+1
           INDA=INDXA+POP(IRREP3D,1)
           INDI=INDXI
           GINDXL=INDXF(INDB,INDA,MOPOP(IRREP1M))
           GINDXR=INDXF(INDI,INDJ,MOPOP(IRREP3M))
           GINDX0=INDXF(GINDXL,GINDXR,MAXL)
           GINDX =GINDX0+IOFFSET
           GAMMA(GINDX)=GAMMA(GINDX)+X
250       CONTINUE
240      CONTINUE
230     CONTINUE
220    CONTINUE
C
C NOW PUT IN THE OOVV CONTRIBUTION
C
       ILOGREC=IOFFSR(IRREP3D)
       IFIRST=IOFFSL(IRREP1D)
       NUMJ=POP(IRREP3D,2)
       NUMB=VRT(IRREP2D,1)
       NUMI=POP(IRREP1D,2)
       NUMA=VRT(IRREP4D,1) 
       MAXL=MOPOP(IRREP1M)*MOPOP(IRREP2M)
       DO 260 INDXJ=1,NUMJ
        DO 270 INDXB=1,NUMB
         INDJ=INDXJ
         INDB=INDXB+POP(IRREP2D,1)
         CALL GETLST(BUF,ILOGREC,1,1,IRREPGET,LISTAIBJ)
         ILOGREC=ILOGREC+1
         IPOS=IOFFSL(IRREP1D)
         DO 280 INDXI=1,NUMI
          DO 290 INDXA=1,NUMA
           X=BUF(IPOS)*FAIBJ
           IPOS=IPOS+1
           INDA=INDXA+POP(IRREP4D,1)
           INDI=INDXI
           GINDXL=INDXF(INDI,INDJ,MOPOP(IRREP1M))
           GINDXR=INDXF(INDB,INDA,MOPOP(IRREP3M))
           GINDX0=INDXF(GINDXL,GINDXR,MAXL)
           GINDX =GINDX0+IOFFSET
           GAMMA(GINDX)=GAMMA(GINDX)+X
290       CONTINUE
280      CONTINUE
270     CONTINUE
260    CONTINUE
100   CONTINUE
      RETURN
      END
