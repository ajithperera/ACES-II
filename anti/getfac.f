
      SUBROUTINE GETFAC(IRREPA,IRREPB,IRREPC,IRREPD,IPTR,FACTOR)
      IMPLICIT INTEGER (A-Z)
      DOUBLE PRECISION FACTOR,ONE,ONEM
      DIMENSION IPTR(4)
      DATA ONE  / 1.0/
      DATA ONEM /-1.0/
C
      FACTOR=ONE
      IPTR(1)=1
      IPTR(2)=2
      IPTR(3)=3
      IPTR(4)=4
C
      IF(IRREPA.GT.IRREPB)THEN
       ITMP=IRREPA
       IRREPA=IRREPB
       IRREPB=ITMP
       IPTR(2)=1
       IPTR(1)=2
       FACTOR=FACTOR*ONEM
      ENDIF
C
      IF(IRREPC.GT.IRREPD)THEN
       ITMP=IRREPC
       IRREPC=IRREPD
       IRREPD=ITMP
       IPTR(4)=3
       IPTR(3)=4
       FACTOR=FACTOR*ONEM
      ENDIF
C
      RETURN
      END
