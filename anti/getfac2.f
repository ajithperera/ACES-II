      SUBROUTINE GETFAC2(IRREPA,IRREPB,IPTR,FACTOR)
      IMPLICIT INTEGER (A-Z)
      DOUBLE PRECISION FACTOR,ONE,ONEM
      DIMENSION IPTR(2)
      DATA ONE  / 1.0/
      DATA ONEM /-1.0/
C
      FACTOR=ONE
      IPTR(1)=1
      IPTR(2)=2
C
      IF(IRREPA.GT.IRREPB)THEN
       ITMP=IRREPA
       IRREPA=IRREPB
       IRREPB=ITMP
       IPTR(2)=1
       IPTR(1)=2
       FACTOR=FACTOR*ONEM
      ENDIF
C
      RETURN
      END
