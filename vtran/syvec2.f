      SUBROUTINE SYVEC2(POP,IRREP,NORB,NIRREP,NOCCA,NOCCB,IUHF)
C
C SETS UP THE OCCUPIED AND VIRTUAL OCCUPATION VECTORS.
C
      IMPLICIT INTEGER (A-Z)
      DIMENSION POP(NIRREP),IRREP(NORB)
C****
      CALL GETREC(20,'JOBARC','IRREPALP',NORB,IRREP)
 
      CALL IZERO(POP,NIRREP)
      DO 15 I=1,NIRREP
       DO 25 J=1,NOCCA
        IF(IRREP(J).EQ.I)POP(I)=POP(I)+1
25     CONTINUE
15    CONTINUE
      CALL PUTREC(20,'JOBARC','SYMPOPOA',NIRREP,POP)
 
 
      CALL IZERO(POP,NIRREP)
      DO 35 I=1,NIRREP
       DO 45 J=NOCCA+1,NORB
        IF(IRREP(J).EQ.I)POP(I)=POP(I)+1
45     CONTINUE
35    CONTINUE
      CALL PUTREC(20,'JOBARC','SYMPOPVA',NIRREP,POP)
C****
 
      IF(IUHF.EQ.0)THEN
       CALL GETREC(20,'JOBARC','IRREPALP',NORB,IRREP)
      ELSE
       CALL GETREC(20,'JOBARC','IRREPBET',NORB,IRREP)
      ENDIF
 
      CALL IZERO(POP,NIRREP)
      DO 115 I=1,NIRREP
       DO 125 J=1,NOCCB
        IF(IRREP(J).EQ.I)POP(I)=POP(I)+1
125    CONTINUE
115   CONTINUE
      CALL PUTREC(20,'JOBARC','SYMPOPOB',NIRREP,POP)
 
      CALL IZERO(POP,NIRREP)
      DO 135 I=1,NIRREP
       DO 145 J=NOCCB+1,NORB
        IF(IRREP(J).EQ.I)POP(I)=POP(I)+1
145    CONTINUE
135   CONTINUE
      CALL PUTREC(20,'JOBARC','SYMPOPVB',NIRREP,POP)
 
      RETURN
      END
