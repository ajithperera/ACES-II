      SUBROUTINE MSZPRI(AMAT2,ISCF)
      IMPLICIT DOUBLE PRECISION(A-H,O-Z)
      DIMENSION AMAT2(6),AMAT(3,3)
C      FACTOR CONVERTS FROM AU**3 TO ANGSTROM**3
      PARAMETER(FACTOR=0.148 184 D0)
      PARAMETER(LUCMD=5, LUPRI=6)
      CHARACTER*2 LAB(3)
C
      DATA ONE,FOUR,FOURTH /1.D0,4.D0,-0.25D0/
C
      AMAT(1,1)=FOURTH*AMAT2(1)
      AMAT(2,1)=FOURTH*AMAT2(2)
      AMAT(3,1)=FOURTH*AMAT2(3)
      AMAT(1,2)=FOURTH*AMAT2(2)
      AMAT(1,3)=FOURTH*AMAT2(3)
      AMAT(2,2)=FOURTH*AMAT2(4)
      AMAT(2,3)=FOURTH*AMAT2(5)
      AMAT(3,2)=FOURTH*AMAT2(5)
      AMAT(3,3)=FOURTH*AMAT2(6)
C
      LAB(1)='Bx'
      LAB(2)='By'
      LAB(3)='Bz'
      WRITE(LUPRI, 1000) LAB
      WRITE(LUPRI, 2000) LAB(1),AMAT(1,1),AMAT(1,2),
     &                          AMAT(1,3)
       WRITE(LUPRI, 2000) LAB(2),AMAT(2,1),AMAT(2,2),
     &                           AMAT(2,3)
       WRITE(LUPRI, 2000) LAB(3),AMAT(3,1),AMAT(3,2),
     &                           AMAT(3,3)
      WRITE(LUPRI, '(/)')
C
C SAVE SCF-SUSCEPYIBILITY ON FILE CHI
C
      IF(ISCF.EQ.1) THEN
       OPEN(UNIT=82,FILE='CHISCF',STATUS='UNKNOWN',FORM='FORMATTED')
      ELSE
       OPEN(UNIT=82,FILE='CHI',STATUS='UNKNOWN',FORM='FORMATTED')
      ENDIF
      REWIND(82)
      WRITE(82,3000) (AMAT(I,1),I=1,3)     
      WRITE(82,3000) (AMAT(I,2),I=1,3)     
      WRITE(82,3000) (AMAT(I,3),I=1,3)
3000  FORMAT(3F20.10)
      CLOSE(UNIT=82,STATUS='KEEP')
C
      RETURN
1000  FORMAT(15X,3(10X,A2)/)
2000  FORMAT(12X,A2,3X,3F12.6)
      END
