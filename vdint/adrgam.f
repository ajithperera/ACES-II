      DOUBLE PRECISION FUNCTION ADRGAM(IJ,KL,IJN,IJADD)
      IMPLICIT DOUBLE PRECISION(A-H,O-Z)
      DIMENSION IJN(1),IJADD(1)
C
      IJKLAD=0
      IF(IJ.NE.1) THEN
       MAX=IJ-1
       DO 10 M=1,MAX
        IJKLAD=IJKLAD+IJN(M)*IJADD(M)
10     CONTINUE
       IF(KL.NE.1) THEN
        IJKLAD=IJKLAD+IJN(IJ)*IJADD(KL-1)
       ENDIF
      ENDIF
      ADRGAM=IJKLAD
C
      RETURN
      END
