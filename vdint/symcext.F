      SUBROUTINE SYMCEXT(CFULL,CSYM,NBAST,NBAS,NMOT,NMO,SCF,FOCK,ISPIN)
      IMPLICIT DOUBLE PRECISION(A-H,O-Z)
      INTEGER DIRPRD,POP,VRT
      LOGICAL SCF,FOCK
      DIMENSION CFULL(NBAST,NMOT),CSYM(*),NBAS(8),NMO(8)
      COMMON/FLAGS/IFLAGS(100)
      COMMON/SYMINF/NSTART,NIRREP,IRREPA(255,2),DIRPRD(8,8)
      COMMON/SYM/POP(8,2),VRT(8,2),NT(2),NF1(2),NF2(2)

      IF((.NOT.SCF.OR.IFLAGS(3).EQ.2).AND.(.NOT.FOCK)) THEN
       CALL REORDCEXT(CFULL,CSYM,NBAST,NMOT,POP(1,ISPIN),VRT(1,ISPIN),
     &                NBAS)
      ENDIF
      IOFFR=1
      IOFFC=0
      IOFFP=1
      DO 10 IRREP=1,NIRREP
       DO 20 IMO=1,NMO(IRREP)
        IOFFC=IOFFC+1
        LENGTH=NBAS(IRREP)
        CALL SCOPY(LENGTH,CFULL(IOFFR,IOFFC),1,CSYM(IOFFP),1)
        IOFFP=IOFFP+LENGTH
20     CONTINUE
       IOFFR=IOFFR+NBAS(IRREP)
10    CONTINUE
      RETURN
      END 
