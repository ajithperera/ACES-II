      SUBROUTINE SYMF(SCR,FT,NBASIS,ISPIN)
      IMPLICIT DOUBLE PRECISION(A-H,O-Z)
      INTEGER POP,VRT,DIRPRD
C
      DIMENSION SCR(1),FT(1)
C
      COMMON/MACHSP/IINTLN,IFLTLN,IINTFP,IALONE,IBITWD
      COMMON/SYM/POP(8,2),VRT(8,2),NT(2),NF1(2),NF2(2)
      COMMON/SYMINF/NSTART,NIRREP,IRREPA(255,2),DIRPRD(8,8)
C
      IF(NIRREP.EQ.1) RETURN
C
c YAU : old
c     CALL ICOPY(NBASIS*IINTFP,FT,1,SCR,1)
c YAU : new
      CALL DCOPY(NBASIS,FT,1,SCR,1)
c YAU : end
C
      IOFFO=1
      IOFFV=1
      DO 10 IRREP=1,NIRREP
       IOFFV=IOFFV+POP(IRREP,ISPIN)
10    CONTINUE
C
      IOFFT=1
C
      DO 20 IRREP=1,NIRREP
C
c YAU : old
c      CALL ICOPY(IINTFP*POP(IRREP,ISPIN),SCR(IOFFO),1,FT(IOFFT),1)
c YAU : new
       CALL DCOPY(POP(IRREP,ISPIN),SCR(IOFFO),1,FT(IOFFT),1)
c YAU : end
C
       IOFFT=IOFFT+POP(IRREP,ISPIN)
       IOFFO=IOFFO+POP(IRREP,ISPIN)
C
c YAU : old
c      CALL ICOPY(IINTFP*VRT(IRREP,ISPIN),SCR(IOFFV),1,FT(IOFFT),1)
c YAU : new
       CALL DCOPY(VRT(IRREP,ISPIN),SCR(IOFFV),1,FT(IOFFT),1)
c YAU : end
C
       IOFFT=IOFFT+VRT(IRREP,ISPIN)
       IOFFV=IOFFV+VRT(IRREP,ISPIN)
C
20    CONTINUE 
C
      RETURN
      END
