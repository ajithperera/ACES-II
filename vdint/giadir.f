      SUBROUTINE GIADIR(CAEQCB,CCEQCD,
     &                  MCCINT,MAXDER,IPRINT)
      IMPLICIT DOUBLE PRECISION(A-H,O-Z)
      LOGICAL CAEQCB,CCEQCD,LJUNK
      LOGICAL DC101,DC1H1,DC1E1,DC2H1,DC2E1,
     &        DC102,DC1H2,DC1E2,DC2H2,DC2E2,
     &        DPATH1,DPATH2
      COMMON/SUBDIR/DC101,DC1H1,DC1E1,DC2H1,DC2E1,
     &              DC102,DC1H2,DC1E2,DC2H2,DC2E2,
     &              DPATH1,DPATH2,NTOTAL
      COMMON/DHOADR/IHOVEC(18)
      COMMON/EXPCOM/SIGN(12),IJUNK(8),LJUNK(5),NINTYP,NCCINT
C
      NCCINT=MCCINT
C
      DPATH1=.FALSE.
      DPATH2=.FALSE.
C
      NINTYP=6
C
      IF(.NOT.CAEQCB) THEN
C
       DPATH2=.TRUE.
C
      ENDIF
C
      IF(.NOT.CCEQCD) THEN
C
       DPATH1=.TRUE.
C
      ENDIF
C
      NTOTAL=NINTYP*NCCINT 
C
       DO 100 I=1,18
        IHOVEC(I)=(I-1)*NCCINT
100    CONTINUE
C
      RETURN
      END
