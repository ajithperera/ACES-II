
     
      SUBROUTINE KILLMO
C
C THIS ROUTINE DISPENSES WITH THE MOINTS FILE AND ZEROS OUT ALL OF
C  THE ASSOCIATED LIST POINTERS
C
CEND
      LOGICAL YESNO
      CHARACTER*80 FNAME
      COMMON /LISTS/ MOIO(10,500),MOIOWD(10,500),MOIOSZ(10,500),
     &              MOIODS(10,500),MOIOFL(10,500)
      INQUIRE(UNIT=50,OPENED=YESNO)
      IF(.NOT.YESNO)THEN
       CALL GFNAME('MOINTS  ',FNAME,ILENGTH) 
       OPEN(UNIT=50,FILE=FNAME(1:ILENGTH),STATUS='OLD',
     &      FORM='UNFORMATTED')
      ENDIF
      CLOSE(UNIT=50,STATUS='DELETE')
      CALL IZERO(MOIO,1000)
      CALL IZERO(MOIOWD,1000)
      CALL IZERO(MOIODS,1000)
      CALL IZERO(MOIOSZ,1000)
      CALL IZERO(MOIOFL,1000)
      RETURN
      END
